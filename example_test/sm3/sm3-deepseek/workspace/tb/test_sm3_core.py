# ─── Clock management ───────────────────────────────────────────────────

async def ensure_clock(dut):
    """Start clock. Must be called at the start of every test."""
    clock = Clock(dut.clk, CLK_PERIOD_NS, unit="ns")
    cocotb.start_soon(clock.start())
    await RisingEdge(dut.clk)


# ─── Helpers ────────────────────────────────────────────────────────────

async def reset_dut(dut):
    """Apply async reset (active-low rst_n) and wait for outputs to settle."""
    global FAIL_COUNT
    dut.rst_n.value = 0
    for name in INPUT_PORTS:
        try:
            sig = getattr(dut, name)
            if hasattr(sig, 'value') and hasattr(sig, 'setimmediatevalue'):
                sig.value = 0
        except Exception:
            pass
    for valid_name in HANDSHAKE_PORTS:
        try:
            getattr(dut, valid_name).value = 0
        except Exception:
            pass
    await ClockCycles(dut.clk, 3)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)


def check(dut, expected, signal, test_name, cycle):
    """Raise AssertionError on mismatch. signal is a dut handle."""
    global FAIL_COUNT
    actual = int(signal.value)
    if expected != actual:
        FAIL_COUNT += 1
        width = len(signal)
        msg = (
            f"[FAIL] {test_name} cycle={cycle} {signal._name}: "
            f"expected=0x{expected:0{(width + 3) // 4}x} got=0x{actual:0{(width + 3) // 4}x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)


async def wait_ready(dut, max_cycles=TIMEOUT_CYCLES):
    """Poll for ready=1, return cycle count waited. Raises on timeout."""
    for i in range(max_cycles):
        await RisingEdge(dut.clk)
        if int(dut.ready.value) == 1:
            return i + 1
    raise AssertionError(f"Timeout after {max_cycles} cycles waiting for ready")


async def wait_valid(dut, valid_name='hash_valid', max_cycles=200):
    """Poll for valid=1, return cycle count waited."""
    valid_sig = getattr(dut, valid_name)
    for i in range(max_cycles):
        await RisingEdge(dut.clk)
        if int(valid_sig.value) == 1:
            return i + 1
    return -1  # timeout


async def drive_sm3_inputs(dut, msg_block_val, is_last_val):
    """Drive SM3 inputs with proper data hold timing.

    Protocol:
      1. Set msg_block/is_last BEFORE valid (data must be stable at posedge)
      2. Wait for ready=1
      3. Drive msg_valid=1 for exactly 1 cycle
      4. Keep msg_block and is_last stable for 3 more cycles
      5. Clear data inputs
    """
    # Set data BEFORE the handshake so it's stable at the msg_valid posedge
    dut.msg_block.value = msg_block_val
    dut.is_last.value = is_last_val

    await wait_ready(dut)

    # Drive valid for 1 cycle
    dut.msg_valid.value = 1
    await RisingEdge(dut.clk)
    dut.msg_valid.value = 0

    # Hold data stable for 3 more cycles (msg_valid→FSM CALC→load_en→w_gen latches)
    for _ in range(3):
        await RisingEdge(dut.clk)

    # Safe to clear data
    dut.msg_block.value = 0
    dut.is_last.value = 0


