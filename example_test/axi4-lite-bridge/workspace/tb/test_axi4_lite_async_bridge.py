@cocotb.test()
async def test_timing_contract(dut):
    """Test 10: Verify timing contract invariants at runtime.

    Checks that registered outputs remain stable between posedge and negedge.
    """
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # Registered outputs from spec.json timing_contract
    REGISTERED_OUTPUTS = [
        "s_axi_awready", "s_axi_wready", "s_axi_bvalid", "s_axi_bresp",
        "s_axi_arready", "s_axi_rvalid", "s_axi_rdata", "s_axi_rresp",
        "m_axi_awvalid", "m_axi_awaddr", "m_axi_awprot",
        "m_axi_wvalid", "m_axi_wdata", "m_axi_wstrb",
        "m_axi_bready",
        "m_axi_arvalid", "m_axi_araddr", "m_axi_arprot",
        "m_axi_rready",
    ]

    violations = []
    for cycle in range(20):
        # Sample registered outputs at posedge of s_axi_aclk
        for port_name in REGISTERED_OUTPUTS:
            try:
                sig = getattr(dut, port_name)
                posedge_val = int(sig.value)
                await FallingEdge(dut.s_axi_aclk)
                negedge_val = int(sig.value)
                if posedge_val != negedge_val:
                    violations.append(
                        f"cycle={cycle} {port_name}: changed between "
                        f"posedge ({posedge_val}) and negedge ({negedge_val}) "
                        f"-- registered output must be stable"
                    )
            except (AttributeError, ValueError):
                pass
        await RisingEdge(dut.s_axi_aclk)

        if any(v.startswith("cycle") for v in violations):
            break

    if violations:
        for v in violations[:5]:
            dut._log.error(f"[TIMING CONTRACT] {v}")
        FAIL_COUNT += len(violations)
        raise AssertionError(f"Timing contract violated: {violations[0]}")
    else:
        dut._log.info("[TIMING CONTRACT] PASS -- all invariants hold")


