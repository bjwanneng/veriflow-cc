async def axi_write(dut, addr, data, strb=0xF, prot=0, timeout_cycles=200):
    """Execute a single AXI4-Lite write transaction on the slave-side interface.

    Drives AW and W channels simultaneously (legal in AXI), waits for
    both handshakes, then waits for B response.

    Returns: bresp (int)
    """
    # Drive AW channel
    dut.s_axi_awaddr.value = addr
    dut.s_axi_awprot.value = prot
    dut.s_axi_awvalid.value = 1

    # Drive W channel
    dut.s_axi_wdata.value = data
    dut.s_axi_wstrb.value = strb
    dut.s_axi_wvalid.value = 1

    # Drive B ready (accept response when it comes)
    dut.s_axi_bready.value = 1

    # Wait for AW handshake
    aw_done = False
    w_done = False
    for _ in range(timeout_cycles):
        await RisingEdge(dut.s_axi_aclk)
        if not aw_done and int(dut.s_axi_awready.value) == 1:
            dut.s_axi_awvalid.value = 0
            aw_done = True
        if not w_done and int(dut.s_axi_wready.value) == 1:
            dut.s_axi_wvalid.value = 0
            w_done = False  # Keep valid until both done -- actually deassert
            w_done = True
            dut.s_axi_wvalid.value = 0
        if aw_done and w_done:
            break
    else:
        raise AssertionError(
            f"[TIMEOUT] axi_write: AW({'done' if aw_done else 'pending'}) "
            f"W({'done' if w_done else 'pending'}) after {timeout_cycles} s_clk cycles"
        )

    # Clear data after handshake
    dut.s_axi_awaddr.value = 0
    dut.s_axi_awprot.value = 0
    dut.s_axi_wdata.value = 0
    dut.s_axi_wstrb.value = 0

    # Wait for B response
    for _ in range(timeout_cycles):
        await RisingEdge(dut.s_axi_aclk)
        if int(dut.s_axi_bvalid.value) == 1:
            bresp = int(dut.s_axi_bresp.value)
            dut.s_axi_bready.value = 0
            return bresp

    raise AssertionError(
        f"[TIMEOUT] axi_write: B channel response not received after "
        f"{timeout_cycles} s_clk cycles"
    )


async def axi_read(dut, addr, prot=0, timeout_cycles=200):
    """Execute a single AXI4-Lite read transaction on the slave-side interface.

    Returns: (rdata, rresp)
    """
    # Drive AR channel
    dut.s_axi_araddr.value = addr
    dut.s_axi_arprot.value = prot
    dut.s_axi_arvalid.value = 1

    # Drive R ready
    dut.s_axi_rready.value = 1

    # Wait for AR handshake
    for _ in range(timeout_cycles):
        await RisingEdge(dut.s_axi_aclk)
        if int(dut.s_axi_arready.value) == 1:
            dut.s_axi_arvalid.value = 0
            dut.s_axi_araddr.value = 0
            dut.s_axi_arprot.value = 0
            break
    else:
        raise AssertionError(
            f"[TIMEOUT] axi_read: AR handshake not completed after "
            f"{timeout_cycles} s_clk cycles"
        )

    # Wait for R response
    for _ in range(timeout_cycles):
        await RisingEdge(dut.s_axi_aclk)
        if int(dut.s_axi_rvalid.value) == 1:
            rdata = int(dut.s_axi_rdata.value)
            rresp = int(dut.s_axi_rresp.value)
            dut.s_axi_rready.value = 0
            return rdata, rresp

    raise AssertionError(
        f"[TIMEOUT] axi_read: R channel response not received after "
        f"{timeout_cycles} s_clk cycles"
    )


