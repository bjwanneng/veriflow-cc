@cocotb.test()
async def test_reset(dut):
    """Test 1: Reset behavior -- outputs quiescent after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset: ready should be 1 (IDLE state), hash_valid=0, hash_out=IV
    ready_val = int(dut.ready.value)
    if ready_val != 1:
        FAIL_COUNT += 1
        dut._log.error(f"[RESET] ready={ready_val} after reset, expected 1")

    hash_valid_val = int(dut.hash_valid.value)
    if hash_valid_val != 0:
        FAIL_COUNT += 1
        dut._log.error(f"[RESET] hash_valid={hash_valid_val} after reset, expected 0")

    if ready_val == 1 and hash_valid_val == 0:
        dut._log.info("[PASS] reset behavior correct: ready=1, hash_valid=0")


@cocotb.test()
async def test_hash_abc(dut):
    """Test 2: SM3('abc') -- GM/T 0004-2012 official test vector.

    Expected: 66c7f0f462eeedd9d1f2d46bdc10e4e24167c4875cf2f7a2297da02b8f4ba8e0
    """
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    expected_hash = 0x66c7f0f462eeedd9d1f2d46bdc10e4e24167c4875cf2f7a2297da02b8f4ba8e0
    msg_block_hex = (
        "61626380000000000000000000000000"
        "00000000000000000000000000000000"
        "00000000000000000000000000000000"
        "00000000000000000000000000000018"
    )
    msg_block_val = int(msg_block_hex, 16)

    dut._log.info("[TEST_ABC] Driving padded 'abc' block")
    await drive_block(dut, msg_block_val, is_last_val=1)

    # Wait for hash_valid
    cycles_waited = await wait_hash_valid(dut, max_cycles=200)
    assert cycles_waited >= 0, "Timeout waiting for hash_valid on 'abc' test"

    # Read hash_out at the SAME RisingEdge where hash_valid=1
    actual_hash = int(dut.hash_out.value) & MASK256
    dut._log.info(
        f"[TEST_ABC] hash_valid after {cycles_waited} cycles, "
        f"hash_out=0x{actual_hash:064x}"
    )

    if actual_hash != expected_hash:
        FAIL_COUNT += 1
        msg = (
            f"[FAIL] test_hash_abc: hash mismatch\n"
            f"  expected: 0x{expected_hash:064x}\n"
            f"  actual:   0x{actual_hash:064x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(f"[PASS] test_hash_abc: hash_out matches expected")


