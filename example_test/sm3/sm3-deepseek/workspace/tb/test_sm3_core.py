@cocotb.test()
async def test_layered(dut):
    """Per-cycle top-level output comparison against golden model trace.

    Drives the "abc" test vector, then compares ready, hash_valid, and hash_out
    against golden_model.py trace on every cycle.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_layered: golden_model.py not found")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Get golden trace
    golden_trace = golden_run()
    if not golden_trace:
        dut._log.info("[SKIP] test_layered: golden_run() returned empty")
        return

    dut._log.info(f"[LAYERED] golden trace: {len(golden_trace)} cycles")
    dut._log.info(f"[LAYERED] DRIVE_PHASE_CYCLES = {DRIVE_PHASE_CYCLES}")

    # Drive the "abc" test vector
    await drive_msg_block(dut, MSG_ABC, is_last_flag=1)

    # Align golden trace with RTL after drive phase
    # Golden trace cycle 0 = initial IDLE (reset state)
    # Golden trace cycle 1 = IDLE->CALC transition (after msg_valid+ready handshake)
    # After drive_msg_block(), we've consumed the handshake and hold cycles.
    # With DRIVE_PHASE_CYCLES=1, we skip golden cycle 0 and start comparing
    # at golden cycle 1 (which corresponds to post-drive RTL state).
    start_offset = 1 + DRIVE_PHASE_CYCLES
    compare_trace = golden_trace[start_offset:]

    first_divergence = None
    cycles_checked = 0

    # Signals to compare at top level
    top_signals = ["ready", "hash_valid", "hash_out"]

    for cycle_idx, expected in enumerate(compare_trace):
        await RisingEdge(dut.clk)

        for sig_name in top_signals:
            expected_val = expected.get(sig_name)
            if expected_val is None:
                continue
            sig = getattr(dut, sig_name, None)
            if sig is None:
                continue
            actual_val = int(sig.value)

            if actual_val != expected_val:
                first_divergence = {
                    "cycle": cycle_idx,
                    "signal": sig_name,
                    "expected": expected_val,
                    "actual": actual_val,
                }
                break

        cycles_checked += 1
        if first_divergence:
            break

    if first_divergence:
        div = first_divergence
        FAIL_COUNT += 1
        msg = (
            f"[LAYERED] FIRST DIVERGENCE at relative cycle {div['cycle']}: "
            f"{div['signal']} expected=0x{div['expected']:x} got=0x{div['actual']:x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(f"[LAYERED] PASS — {cycles_checked} cycles matched golden model")


