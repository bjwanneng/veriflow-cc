# --- Helpers --------------------------------------------------------------

async def reset_dut(dut):
    """Apply active-low reset and wait for outputs to settle."""
    global FAIL_COUNT
    # Assert reset (active-low: rst_n=0)
    dut.rst_n.value = 0
    # Zero all input ports
    for name in INPUT_PORTS:
        try:
            sig = getattr(dut, name)
            if hasattr(sig, 'value') and hasattr(sig, 'setimmediatevalue'):
                sig.value = 0
        except Exception:
            pass
    # Hold reset for 3 cycles
    await ClockCycles(dut.clk, 3)
    # Release reset
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)


def check(dut, expected, signal, test_name, cycle):
    """Raise AssertionError on mismatch. signal is a dut handle."""
    global FAIL_COUNT
    actual = int(signal.value)
    if expected != actual:
        FAIL_COUNT += 1
        width = len(signal)
        msg = (
            f"[FAIL] {test_name} cycle={cycle} {signal._name}: "
            f"expected=0x{expected:0{(width + 3) // 4}x} got=0x{actual:0{(width + 3) // 4}x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)


async def drive_inputs(dut, input_values, valid_name="valid_in",
                       ready_name="ready_out",
                       hold_data_cycles=None):
    """Wait for ready, then drive valid=1 with input values.

    SM3-specific: msg_valid uses single_cycle handshake with ready.
    msg_block and is_last must remain stable for DRIVE_PHASE_CYCLES
    additional cycles after msg_valid deassertion.
    """
    if hold_data_cycles is None:
        hold_data_cycles = DRIVE_PHASE_CYCLES

    # Wait for ready (DUT must be in IDLE state)
    timeout = TIMEOUT_CYCLES
    ready_sig = getattr(dut, ready_name)
    while int(ready_sig.value) != 1 and timeout > 0:
        await RisingEdge(dut.clk)
        timeout -= 1
    assert timeout > 0, "Timeout waiting for ready before drive_inputs"

    # Drive all inputs at once
    for port_name, value in input_values.items():
        getattr(dut, port_name).value = value
    getattr(dut, valid_name).value = 1

    # Wait one rising edge — DUT samples inputs at this posedge
    await RisingEdge(dut.clk)

    # Single_cycle: deassert valid immediately after sampling
    getattr(dut, valid_name).value = 0

    # Hold data for DRIVE_PHASE_CYCLES so load_en (NBA delayed) sees stable data
    for _ in range(hold_data_cycles):
        await RisingEdge(dut.clk)

    # Clear data inputs
    for port_name in input_values:
        getattr(dut, port_name).value = 0


async def wait_valid(dut, valid_name="valid_out", max_cycles=TIMEOUT_CYCLES):
    """Poll for valid=1, return cycle count waited. Returns -1 on timeout."""
    valid_sig = getattr(dut, valid_name)
    for i in range(max_cycles):
        await RisingEdge(dut.clk)
        if int(valid_sig.value) == 1:
            return i + 1
    return -1


def resolve_vpi_path(dut, sig_name):
    """Resolve a golden trace signal name to an actual VPI object.

    Returns (obj, actual_val) or None if not found.
    """
    vpi_path = GOLDEN_TRACE_TO_VPI_PATH.get(sig_name)
    if vpi_path is None:
        # Fallback: try raw name
        try:
            sig = getattr(dut, sig_name)
            return int(sig.value), sig_name
        except AttributeError:
            return None

    try:
        obj = dut
        for part in vpi_path:
            if '[' in part:
                arr_name, idx_str = part.split('[')
                idx = int(idx_str.rstrip(']'))
                obj = getattr(obj, arr_name)
                obj = obj[idx]
            else:
                obj = getattr(obj, part)
        return int(obj.value), ".".join(vpi_path)
    except (AttributeError, IndexError):
        return None


