@cocotb.test()
async def test_handshake(dut):
    """Verify valid-ready handshake: msg_valid is accepted only when ready=1."""
    await ensure_clock(dut)
    await reset_dut(dut)

    # Initially ready should be 1
    assert int(dut.ready.value) == 1, "ready not asserted after reset"

    # Drive msg_valid=1 while ready=1 — FSM should accept and go to CALC
    dut.msg_valid.value = 1
    dut.msg_block.value = MSG_ABC
    dut.is_last.value = 1
    await RisingEdge(dut.clk)

    # After sampling, ready should go to 0 (FSM transitioned to CALC)
    # Note: ready is registered, so it's still 1 at this RisingEdge.
    # It will be 0 after the NBA.
    dut.msg_valid.value = 0
    await RisingEdge(dut.clk)

    # Now ready should be 0 (FSM in CALC)
    # But hash_valid should not yet be asserted
    assert int(dut.hash_valid.value) == 0, "hash_valid asserted too early"

    # Clear data after hold
    dut.msg_block.value = 0
    dut.is_last.value = 0

    # Wait for hash_valid
    for _ in range(TIMEOUT_CYCLES):
        await RisingEdge(dut.clk)
        if int(dut.hash_valid.value) == 1:
            break
    else:
        dut._log.error("Timeout waiting for hash_valid")
        return

    dut._log.info("[PASS] test_handshake: valid-ready protocol and hash completion verified")


