    m_clk = Clock(dut.m_axi_aclk, M_CLK_PERIOD_NS, unit="ns")
    cocotb.start_soon(s_clk.start())
    cocotb.start_soon(m_clk.start())
    # Wait for both clocks to produce at least one edge
    await RisingEdge(dut.s_axi_aclk)
    await RisingEdge(dut.m_axi_aclk)


