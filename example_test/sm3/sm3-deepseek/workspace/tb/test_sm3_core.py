async def test_internal_signals(dut):
    """Per-cycle internal register comparison against golden model trace.

    This is the KEY verification test. Reads RTL internal registers via VPI
    and compares them against golden_model.py trace on EVERY clock cycle.

    Internal signals checked:
      - FSM: state_reg, load_en_reg, calc_en_reg, update_v_en_reg, round_cnt_reg
      - w_gen: w_j, w_prime_j
      - compress: a_reg_reg..h_reg_reg, v0_reg_reg..v7_reg_reg

    The comparison alignment uses DRIVE_PHASE_CYCLES=1.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not found")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Get golden trace
    golden_trace = golden_run()
    if not golden_trace:
        dut._log.info("[SKIP] test_internal_signals: golden_run() returned empty")
        return

    dut._log.info(f"[INTERNAL] golden trace: {len(golden_trace)} cycles")

    # Drive the "abc" test vector
    await drive_msg_block(dut, MSG_ABC, is_last_flag=1)

    # Align: skip golden cycle 0 (reset/IDLE) + DRIVE_PHASE_CYCLES
    start_offset = DRIVE_PHASE_CYCLES
    compare_trace = golden_trace[start_offset:]

    first_divergence = None
    cycles_checked = 0
    total_signals_checked = 0
    signals_not_found = set()

    # Internal signals we want to check
    internal_map = {
        "ready":       "ready",
        "hash_valid":  "hash_valid",
        "hash_out":    "hash_out",
        "fsm_state":   "u_fsm.state_reg",
        "load_en":     "u_fsm.load_en_reg",
        "calc_en":     "u_fsm.calc_en_reg",
        "update_v_en": "u_fsm.update_v_en_reg",
        "round_cnt":   "u_fsm.round_cnt_reg",
        "w_j":         "u_w_gen.w_j",
        "w_prime_j":   "u_w_gen.w_prime_j",
        "A":           "u_compress.a_reg_reg",
        "B":           "u_compress.b_reg_reg",
        "C":           "u_compress.c_reg_reg",
        "D":           "u_compress.d_reg_reg",
        "E":           "u_compress.e_reg_reg",
        "F":           "u_compress.f_reg_reg",
        "G":           "u_compress.g_reg_reg",
        "H":           "u_compress.h_reg_reg",
        "V0":          "u_compress.v0_reg_reg",
        "V1":          "u_compress.v1_reg_reg",
        "V2":          "u_compress.v2_reg_reg",
        "V3":          "u_compress.v3_reg_reg",
        "V4":          "u_compress.v4_reg_reg",
        "V5":          "u_compress.v5_reg_reg",
        "V6":          "u_compress.v6_reg_reg",
        "V7":          "u_compress.v7_reg_reg",
    }

    for cycle_idx, expected in enumerate(compare_trace):
        await RisingEdge(dut.clk)

        for gold_name, rtl_path in internal_map.items():
            expected_val = expected.get(gold_name)
            if expected_val is None:
                continue

            actual_val, _ = await get_hier_signal(dut, rtl_path)
            if actual_val is None:
                signals_not_found.add(rtl_path)
                continue

            total_signals_checked += 1
            if actual_val != expected_val:
                first_divergence = {
                    "cycle": cycle_idx,
                    "gold_name": gold_name,
                    "rtl_path": rtl_path,
                    "expected": expected_val,
                    "actual": actual_val,
                }
                break

        cycles_checked += 1
        if first_divergence:
            break

    # Report signals not found
    if signals_not_found:
        dut._log.warning(
            f"[INTERNAL] {len(signals_not_found)} signal(s) not accessible. "
            f"First few: {sorted(list(signals_not_found))[:8]}"
        )

    # Safety check: ensure we actually compared something
    if total_signals_checked == 0:
        dut._log.error(
            "[INTERNAL] ZERO signals compared — check VPI access and signal names"
        )
        FAIL_COUNT += 1
        raise AssertionError("test_internal_signals: no signals compared")

    if first_divergence:
        div = first_divergence
        FAIL_COUNT += 1
        # Classify the error
        if div["actual"] == 0 and div["expected"] != 0:
            bug_type = "ZERO_OUT (computation produced 0, expected non-zero → init or logic bug)"
        else:
            xor_diff = div["actual"] ^ div["expected"]
            bug_type = (
                f"DATA_MISMATCH (xor_diff=0x{xor_diff:x})"
            )
        msg = (
            f"[INTERNAL] FIRST DIVERGENCE at relative cycle {div['cycle']}: "
            f"{div['gold_name']}({div['rtl_path']}) "
            f"expected=0x{div['expected']:08x} got=0x{div['actual']:08x} "
            f"→ {bug_type}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(
            f"[INTERNAL] PASS — {cycles_checked} cycles, "
            f"{total_signals_checked} signal values matched golden model"
        )


