# ─── Clock management ────────────────────────────────────────────────────

async def ensure_clock(dut):
    """Start clock. Must be called at the start of every test."""
    clock = Clock(dut.clk, CLK_PERIOD_NS, unit=CLK_PERIOD_UNIT)
    cocotb.start_soon(clock.start())
    await RisingEdge(dut.clk)


# ─── Helpers ─────────────────────────────────────────────────────────────

async def reset_dut(dut):
    """Apply synchronous active-low reset and wait for outputs to settle."""
    dut.rst_n.value = 0
    dut.msg_valid.value = 0
    dut.msg_block.value = 0
    dut.is_last.value = 0
    await ClockCycles(dut.clk, 3)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)  # rst_n deassert sampled
    await Timer(1, unit="ns")  # allow NBA to settle


async def drive_msg_block(dut, block_data, is_last_flag=1):
    """Drive a single 512-bit message block via valid-ready handshake.

    Timing:
      1. Wait for ready=1
      2. Drive msg_valid=1, msg_block, is_last simultaneously
      3. Wait 1 cycle — DUT FSM samples handshake, transitions CALC
      4. Deassert msg_valid
      5. Hold msg_block for 1 extra cycle (data hold time)
         This covers pipeline_delay_cycles=1: registered FSM load_en
         arrives at w_gen/compress datapath 1 cycle after FSM transitions.
      6. Clear msg_block

    Returns the cycle count for when driving started.
    """
    # Wait for ready
    for _ in range(TIMEOUT_CYCLES):
        if int(dut.ready.value) == 1:
            break
        await RisingEdge(dut.clk)
    else:
        dut._log.error("Timeout waiting for ready")
        return

    # Drive inputs (cycle 0 of the block transaction)
    dut.msg_valid.value = 1
    dut.msg_block.value = block_data
    dut.is_last.value = is_last_flag
    await RisingEdge(dut.clk)

    # Deassert valid (cycle 1)
    dut.msg_valid.value = 0
    await RisingEdge(dut.clk)

    # Clear data after hold time (cycle 2 — load_en has now propagated)
    dut.msg_block.value = 0
    dut.is_last.value = 0
    await RisingEdge(dut.clk)


async def get_hier_signal(dut, path):
    """Resolve a hierarchical signal path via VPI.

    Supports dotted paths like 'u_fsm.state_reg'.
    Returns (value_int, signal_handle) or (None, None) if not found.
    """
    if '.' not in path:
        sig = getattr(dut, path, None)
        if sig is not None:
            return int(sig.value), sig
        return None, None

    parts = path.split('.')
    obj = dut
    for part in parts:
        obj = getattr(obj, part, None)
        if obj is None:
            return None, None
    try:
        return int(obj.value), obj
    except Exception:
        return None, None


def check_value(actual, expected, signal_name, cycle_idx, test_name):
    """Compare actual vs expected. Returns True if match."""
    global FAIL_COUNT
    if actual == expected:
        return True
    FAIL_COUNT += 1
    width = max(expected.bit_length(), actual.bit_length(), 32)
    fmt_w = (width + 3) // 4
    msg = (
        f"[FAIL] {test_name} cycle={cycle_idx} signal={signal_name}: "
        f"expected=0x{expected:0{fmt_w}x} got=0x{actual:0{fmt_w}x}"
    )
    print(msg)
    return False


