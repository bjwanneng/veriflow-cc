    try:
        rdy = int(dut.ready.value)
        if rdy != 1:
            FAIL_COUNT += 1
            dut._log.error(f"[PROTOCOL] ready={rdy} after reset, expected 1")
        else:
            dut._log.info(f"[PROTOCOL] Check 2 PASS: ready=1 after reset")
    except AttributeError:
        pass

    # Check 3: hash_valid must be a single-cycle pulse (not sticky)
    # Drive a test vector and verify hash_valid returns to 0 after pulsing
    if GOLDEN_AVAILABLE:
        await drive_inputs(dut, {"msg_block": MSG_BLOCK_ABC, "is_last": 1},
                           valid_name="msg_valid", ready_name="ready")

        # Poll for hash_valid
