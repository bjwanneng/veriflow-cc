"""cocotb testbench for sm3_core — golden model per-cycle cross-check.
Generated by VeriFlow-CC vf-tb-gen.

Verification strategy:
  test_reset            — reset behavior: ready=1, hash_valid=0, hash_out=IV
  test_protocol         — valid/ready handshake protocol compliance
  test_layered          — golden model cycle-by-cycle output port comparison
  test_internal_signals — per-cycle internal register comparison (KEY test)
  test_summary          — aggregate pass/fail report

Timing convention:
  Golden trace[N] = POST-NBA register state after posedge N.
  After reset: DUT at golden[0] (post-reset).
  drive_inputs() samples at RisingEdge, DUT advances to golden[1].
  Comparison loop reads BEFORE await, so first read matches golden[1].
  Data inputs held for DRIVE_PHASE_CYCLES+1 cycles after valid deassert.

Clock: 10ns period (100MHz) for simulation simplicity.
Active-low reset: rst_n (0=assert, 1=release).
"""

