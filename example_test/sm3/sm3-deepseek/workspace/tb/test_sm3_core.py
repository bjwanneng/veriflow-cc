@cocotb.test()
async def test_layered(dut):
    """Test 3: End-to-end verification — "abc" test vector.

    Drives the padded "abc" message block, waits for hash_valid,
    and compares hash_out against the expected GM/T 0004-2012 value.
    """
    global FAIL_COUNT

    await ensure_clock(dut)
    await reset_dut(dut)

    dut._log.info("[LAYERED] Driving 'abc' test vector")

    # Drive the "abc" padded block
    await drive_sm3_inputs(dut, ABC_MSG_BLOCK, is_last_val=1)

    # Wait for hash_valid
    waited = await wait_valid(dut, valid_name='hash_valid', max_cycles=100)
    if waited < 0:
        FAIL_COUNT += 1
        dut._log.error("[LAYERED] Timeout waiting for hash_valid")
        raise AssertionError("hash_valid not asserted within 100 cycles")

    dut._log.info(f"[LAYERED] hash_valid asserted after {waited} cycles")

    # hash_out is registered and updated at same NBA as hash_valid.
    # Wait one more cycle for hash_out to be stable.
    await RisingEdge(dut.clk)

    # Compare hash_out
    actual_hash = int(dut.hash_out.value)
    if actual_hash != ABC_EXPECTED_HASH:
        FAIL_COUNT += 1
        msg = (
            f"[LAYERED] Hash mismatch: "
            f"expected=0x{ABC_EXPECTED_HASH:064x} "
            f"got=0x{actual_hash:064x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)

    dut._log.info(f"[LAYERED] PASS — hash_out=0x{actual_hash:064x} matches expected")


@cocotb.test()
async def test_internal_signals(dut):
    """Test 4: Per-cycle internal signal comparison against golden model.

    Compares RTL internal registers (A-H in sm3_compress) against
    golden_model.py per-round expected values.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Get golden model per-round trace
    try:
        expected_rounds = golden_run(0)  # test vector index 0 = "abc"
    except Exception as e:
        dut._log.warning(f"[INTERNAL] golden_run() failed: {e}")
        return

    if not expected_rounds:
        dut._log.info("[SKIP] test_internal_signals: golden model returned no data")
        return

    dut._log.info(f"[INTERNAL] Golden model returned {len(expected_rounds)} rounds")

    # Drive the "abc" test vector
    await drive_sm3_inputs(dut, ABC_MSG_BLOCK, is_last_val=1)

    # Map golden trace keys to VPI hierarchy paths in the RTL
    # The golden model produces keys: A, B, C, D, E, F, G, H, W_j, W_prime_j, SS1, SS2, TT1, TT2
    # RTL internal registers are in sm3_compress submodule
    GOLDEN_TO_VPI = {}
    compress_regs = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H']
    for reg_name in compress_regs:
        # Try direct name first (cocotb may auto-discover)
        GOLDEN_TO_VPI[reg_name] = reg_name

    first_divergence = None
    rounds_checked = 0
    signals_compared = 0
    signals_not_found = []

    # Align: golden model round N corresponds to RTL internal state after NBA of CALC cycle N+1
    # (round 0 happens after load_en, which is 1 cycle after handshake)
    # The first round trace is the state AFTER round 0 computation
    # RTL: posedge with load_en=1 → NBA loads A-H from V → combinational round 0 logic computes
    # → next posedge NBA updates A-H with round 0 result
    # So golden round 0 is visible at posedge 2 after the handshake.

    for round_idx, expected in enumerate(expected_rounds):
        await RisingEdge(dut.clk)

        for sig_name, expected_val in expected.items():
            # Only compare registered state (skip combinational intermediates)
            if sig_name in ('round', 'W_j', 'W_prime_j', 'SS1', 'SS2', 'TT1', 'TT2'):
                continue

            vpi_name = GOLDEN_TO_VPI.get(sig_name)
            if vpi_name is None:
                continue

            try:
                sig = getattr(dut, vpi_name)
                actual_val = int(sig.value)
                signals_compared += 1
                if actual_val != expected_val:
                    first_divergence = {
                        "round": round_idx,
                        "signal": sig_name,
                        "expected": expected_val,
                        "actual": actual_val,
                    }
                    break
            except (AttributeError, IndexError):
                if sig_name not in signals_not_found:
                    signals_not_found.append(sig_name)

        rounds_checked += 1
        if first_divergence:
            break

    # Warn about signals that could not be found
    if signals_not_found:
        dut._log.warning(
            f"[INTERNAL] {len(signals_not_found)} signal(s) not found in DUT hierarchy: "
            f"{signals_not_found}"
        )

    # If we found no signals via direct name, try VPI hierarchy paths
    if signals_compared == 0:
        dut._log.info("[INTERNAL] Trying VPI hierarchy paths (u_compress.A etc.)")
        # Re-drive for a fresh comparison
        await reset_dut(dut)
        await drive_sm3_inputs(dut, ABC_MSG_BLOCK, is_last_val=1)

        for round_idx, expected in enumerate(expected_rounds):
            await RisingEdge(dut.clk)

            for sig_name, expected_val in expected.items():
                if sig_name in ('round', 'W_j', 'W_prime_j', 'SS1', 'SS2', 'TT1', 'TT2'):
                    continue
                try:
                    sig = getattr(dut.u_compress, sig_name)
                    actual_val = int(sig.value)
                    signals_compared += 1
                    if actual_val != expected_val:
                        first_divergence = {
                            "round": round_idx,
                            "signal": sig_name,
                            "expected": expected_val,
                            "actual": actual_val,
                        }
                        break
                except (AttributeError, IndexError):
                    pass

            if first_divergence:
                break

    if signals_compared == 0:
        dut._log.error(
            "[INTERNAL] ZERO signals compared — internal register names "
            "do not match RTL. Check VPI hierarchy paths."
        )
        raise AssertionError("test_internal_signals: no signals compared")

    if first_divergence:
        FAIL_COUNT += 1
        div = first_divergence
        msg = (
            f"[INTERNAL] FIRST DIVERGENCE at round={div['round']} "
            f"signal={div['signal']}: "
            f"expected=0x{div['expected']:08x} got=0x{div['actual']:08x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(
            f"[INTERNAL] PASS — {rounds_checked} rounds, "
            f"{signals_compared} signal values matched"
        )


