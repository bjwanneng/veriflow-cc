    await ensure_clock(dut)
    await reset_dut(dut)

    # Check 1: After reset, hash_valid must be 0
    try:
        hv = int(dut.hash_valid.value)
        if hv != 0:
            FAIL_COUNT += 1
            dut._log.error(f"[PROTOCOL] hash_valid={hv} after reset, expected 0")
        else:
            dut._log.info(f"[PROTOCOL] Check 1 PASS: hash_valid=0 after reset")
    except AttributeError:
        dut._log.warning("[PROTOCOL] hash_valid port not found")

