@cocotb.test()
async def test_01_reset(dut):
    """Test 1: Verify reset behavior — outputs quiescent after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset: out_valid must be 0, out_result must be 0
    ok = True
    if int(dut.out_valid.value) != 0:
        dut._log.error(f"[RESET] out_valid={int(dut.out_valid.value)} after reset, expected 0")
        ok = False
        FAIL_COUNT += 1

    if int(dut.out_result.value) != 0:
        dut._log.error(f"[RESET] out_result={int(dut.out_result.value)} after reset, expected 0")
        ok = False
        FAIL_COUNT += 1

    if int(dut.cfg_ready.value) != 1:
        dut._log.error(f"[RESET] cfg_ready={int(dut.cfg_ready.value)} after reset, expected 1 (CONFIG state)")
        ok = False
        FAIL_COUNT += 1

    if ok:
        dut._log.info("[PASS] reset behavior correct — out_valid=0, out_result=0, cfg_ready=1")


