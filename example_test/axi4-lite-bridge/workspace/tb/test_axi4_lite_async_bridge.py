async def run_test_vector(dut, test_name, tv, slave):
    """Drive a golden model test vector through the DUT and compare results.

    Args:
        dut: cocotb DUT handle
        test_name: test vector name string
        tv: test vector dict from golden_model.TEST_VECTORS
        slave: AxiSlaveBFM instance

    Returns: number of failures (0 = pass)
    """
    global FAIL_COUNT
    failures = 0

    transactions = tv["inputs"]["transactions"]
    master_responses = list(tv["inputs"].get("master_responses", []))
    expected_results = tv["expected"]["transactions"]
    resp_idx = 0

    # Pre-load slave response queues
    for resp in master_responses:
        if "bresp" in resp:
            await slave.b_responses.put(resp)
        elif "rdata" in resp:
            await slave.r_responses.put(resp)

    results = []
    for txn in transactions:
        if txn["type"] == "write":
            addr = txn["addr"]
            data = txn.get("data", 0)
            strb = txn.get("strb", 0xF)
            prot = txn.get("prot", 0)
            bresp = await axi_write(dut, addr, data, strb, prot)
            results.append({
                "type": "write",
                "addr": addr,
                "data": data,
                "bresp": bresp,
            })
            # Small gap between transactions for FIFO crossing
            for _ in range(3):
                await RisingEdge(dut.s_axi_aclk)

        elif txn["type"] == "read":
            addr = txn["addr"]
            prot = txn.get("prot", 0)
            rdata, rresp = await axi_read(dut, addr, prot)
            results.append({
                "type": "read",
                "addr": addr,
                "rdata": rdata,
                "rresp": rresp,
            })
            # Small gap between transactions for FIFO crossing
            for _ in range(3):
                await RisingEdge(dut.s_axi_aclk)

    # Compare results
    if len(results) != len(expected_results):
        dut._log.error(
            f"[{test_name}] Transaction count mismatch: "
            f"got {len(results)}, expected {len(expected_results)}"
        )
        return len(expected_results)

    for i, (actual, expected) in enumerate(zip(results, expected_results)):
        if actual["type"] != expected["type"]:
            dut._log.error(
                _format_txn_mismatch(test_name, i, "type",
                                     expected["type"], actual["type"], 8)
            )
            failures += 1
            continue

        if actual["type"] == "write":
            if actual["bresp"] != expected["bresp"]:
                dut._log.error(
                    _format_txn_mismatch(test_name, i, "bresp",
                                         expected["bresp"], actual["bresp"], 2)
                )
                failures += 1

        elif actual["type"] == "read":
            if actual["rdata"] != expected["rdata"]:
                dut._log.error(
                    _format_txn_mismatch(test_name, i, "rdata",
                                         expected["rdata"], actual["rdata"], 32)
                )
                failures += 1
            if actual["rresp"] != expected["rresp"]:
                dut._log.error(
                    _format_txn_mismatch(test_name, i, "rresp",
                                         expected["rresp"], actual["rresp"], 2)
                )
                failures += 1

    FAIL_COUNT += failures
    return failures


