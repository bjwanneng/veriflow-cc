        in_data_im <= in_im[i];
        @(posedge clk);
      end
      in_valid <= 1'b0;
      in_data_re <= 16'h0000;
      in_data_im <= 16'h0000;
    end
  endtask

  // -------------------------------------------------------
  // Capture and check 64 outputs using out_index
  // -------------------------------------------------------
  task capture_check;
    input [255:0] test_name;
    integer captured, err_re, err_im, pass_cnt, fail_cnt;
    reg [15:0] act_re, act_im;
    reg [5:0] idx;
    begin
      captured = 0;
      pass_cnt = 0;
      fail_cnt = 0;
      for (i = 0; i < 300; i = i + 1) begin
        @(posedge clk);
        if (out_valid === 1'b1) begin
          act_re = out_data_re;
          act_im = out_data_im;
          idx = out_index;
          err_re = act_re - exp_re[idx];
          if (err_re < 0) err_re = -err_re;
          err_im = act_im - exp_im[idx];
          if (err_im < 0) err_im = -err_im;
          if (err_re > TOL || err_im > TOL) begin
            $display("[FAIL] %0s sample[%0d] idx=%0d re=%0d im=%0d exp_re=%0d exp_im=%0d",
                     test_name, captured, idx, $signed(act_re), $signed(act_im),
                     $signed(exp_re[idx]), $signed(exp_im[idx]));
            fail_cnt = fail_cnt + 1;
          end else
            pass_cnt = pass_cnt + 1;
          captured = captured + 1;
          if (captured >= 64) i = 400;
        end
      end
      if (fail_cnt == 0)
        $display("[PASS] %0s: %0d/64 samples matched", test_name, pass_cnt);
      else
        $display("[FAIL] %0s: %0d passed, %0d failed", test_name, pass_cnt, fail_cnt);
      total_pass = total_pass + pass_cnt;
      total_fail = total_fail + fail_cnt;
    end
  endtask

