async def ensure_clocks(dut):
    """Start both async clock domains. MUST be the first await in every test.

    s_axi_aclk at 10 ns (100 MHz), m_axi_aclk at 13 ns (~77 MHz).
    Different frequencies to stress the async crossing.
    """
