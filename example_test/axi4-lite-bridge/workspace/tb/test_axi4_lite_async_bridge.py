# AXI4-Lite Slave BFM (responds on master-side m_axi_* ports)
# Simulates an AXI slave connected to the bridge's master interface.
# Runs as concurrent coroutines.
# ---------------------------------------------------------------------------

class AxiSlaveBFM:
    """Simple AXI4-Lite slave that responds to transactions on the
    master-side interface of the bridge.

    Usage:
        slave = AxiSlaveBFM(dut)
        slave.start()
        # ... run master-side transactions ...
        slave.stop()

    The slave always accepts writes (awready=1, wready=1) and provides
    configurable responses for B and R channels.
    """

    def __init__(self, dut):
        self.dut = dut
        self._running = False
        self._tasks = []
        # Response queues: populated by test, consumed by BFM coroutines
        self.b_responses = asyncio.Queue()   # items: {"bresp": int}
        self.r_responses = asyncio.Queue()   # items: {"rdata": int, "rresp": int}
        # Observed transactions (for debugging)
        self.observed_writes = []  # list of {"addr": int, "data": int, "strb": int}
        self.observed_reads = []   # list of {"addr": int}

    def start(self):
        """Start all slave BFM coroutines."""
        self._running = True
        self._tasks = [
            cocotb.start_soon(self._aw_channel()),
            cocotb.start_soon(self._w_channel()),
            cocotb.start_soon(self._ar_channel()),
        ]

    def stop(self):
        """Signal all coroutines to stop."""
        self._running = False

    async def _aw_channel(self):
        """Handle AW channel: accept write addresses, always ready."""
        dut = self.dut
        while self._running:
            dut.m_axi_awready.value = 1
            await RisingEdge(dut.m_axi_aclk)
            if int(dut.m_axi_awvalid.value) == 1:
                addr = int(dut.m_axi_awaddr.value)
                # Address stored for cross-reference with W channel
                self._last_aw_addr = addr

    async def _w_channel(self):
        """Handle W+B channels: accept write data, provide B response."""
        dut = self.dut
        while self._running:
            dut.m_axi_wready.value = 1
            await RisingEdge(dut.m_axi_aclk)
            if int(dut.m_axi_wvalid.value) == 1:
                data = int(dut.m_axi_wdata.value)
                strb = int(dut.m_axi_wstrb.value)
                addr = getattr(self, '_last_aw_addr', 0)
                self.observed_writes.append({"addr": addr, "data": data, "strb": strb})

                # Wait a couple of m_clk cycles to simulate slave latency,
                # then drive B response
                for _ in range(2):
                    await RisingEdge(dut.m_axi_aclk)

                # Get response from queue or default to OKAY
                try:
                    resp = self.b_responses.get_nowait()
                except asyncio.QueueEmpty:
                    resp = {"bresp": 0}  # RESP_OKAY

                dut.m_axi_bresp.value = resp["bresp"]
                dut.m_axi_bvalid.value = 1
                # Wait for bready from bridge
                for _ in range(200):
                    await RisingEdge(dut.m_axi_aclk)
                    if int(dut.m_axi_bready.value) == 1:
                        break
                dut.m_axi_bvalid.value = 0

    async def _ar_channel(self):
        """Handle AR+R channels: accept read addresses, provide R response."""
        dut = self.dut
        while self._running:
            dut.m_axi_arready.value = 1
            await RisingEdge(dut.m_axi_aclk)
            if int(dut.m_axi_arvalid.value) == 1:
                addr = int(dut.m_axi_araddr.value)
                self.observed_reads.append({"addr": addr})

                # Wait a couple of m_clk cycles for slave latency
                for _ in range(2):
                    await RisingEdge(dut.m_axi_aclk)

                # Get response from queue or default to 0/OKAY
                try:
                    resp = self.r_responses.get_nowait()
                except asyncio.QueueEmpty:
                    resp = {"rdata": 0, "rresp": 0}  # RESP_OKAY

                dut.m_axi_rdata.value = resp["rdata"]
                dut.m_axi_rresp.value = resp["rresp"]
                dut.m_axi_rvalid.value = 1
                # Wait for rready from bridge
                for _ in range(200):
                    await RisingEdge(dut.m_axi_aclk)
                    if int(dut.m_axi_rready.value) == 1:
                        break
                dut.m_axi_rvalid.value = 0


