@cocotb.test()
async def test_layered(dut):
    """Test 3: Layered verification — golden model cycle-by-cycle comparison.

    For algorithm designs with a golden model:
    1. Drive input stimulus from golden model test vectors
    2. Compare DUT output signals against golden model per-cycle expected values
    3. Fail-fast on first mismatch: report cycle, signal, expected vs actual
    4. This quickly isolates WHERE the first computation error occurs

    Skipped if golden_model.py is not available (non-algorithm designs).
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_layered: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Get golden model expected values
    expected_cycles = golden_run()
    if not expected_cycles:
        dut._log.info("[SKIP] test_layered: golden model returned no data")
        return

    dut._log.info(f"[LAYERED] Comparing {len(expected_cycles)} cycles against golden model")

    # IMPORTANT: codegen MUST add drive_inputs() call here with golden model
    # test vectors before the comparison loop. Without driving inputs,
    # the DUT has no stimulus and all outputs will be initial values.
    #
    # Example (codegen fills in):
    #   test_vectors = golden_model.TEST_VECTORS
    #   for vec in test_vectors:
    #       await drive_inputs(dut, vec["inputs"], valid_name="valid_in",
    #                         ready_name="ready_out")
    #
    # If golden_model.py provides input data per cycle, drive it here.
    # If the design processes a single input block, drive once then wait.

    first_divergence = None
    cycles_checked = 0

    # Map golden trace signal names to DUT port names.
    # Golden model uses "_reg" suffix; DUT output ports don't.
    # Codegen populates this from spec.json port names.
    GOLDEN_TO_PORT = {}  # {"ready_reg": "ready", "hash_valid_reg": "hash_valid", ...}

    # Skip golden cycle 0 (reset state) + DRIVE_PHASE_CYCLES offset.
    # Golden cycle 0 is the reset state; comparison starts at the IDLE->active
    # transition cycle (cycle 1), which matches the first comparison RisingEdge.
    compare_cycles = expected_cycles[1 + DRIVE_PHASE_CYCLES:]

    for cycle_idx, expected in enumerate(compare_cycles):
        # Wait one cycle for DUT to produce output
        await RisingEdge(dut.clk)

        # Compare each output signal against golden model
        for sig_name, expected_val in expected.items():
            # Map golden trace name to DUT port name
            dut_name = GOLDEN_TO_PORT.get(sig_name)
            if dut_name is None:
                continue  # not a mapped output port
            try:
                sig = getattr(dut, dut_name)
                actual_val = int(sig.value)
                if actual_val != expected_val:
                    first_divergence = {
                        "cycle": cycle_idx,
                        "signal": sig_name,
                        "expected": expected_val,
                        "actual": actual_val,
                    }
                    break
            except AttributeError:
                pass  # signal not accessible in DUT hierarchy, skip

        cycles_checked += 1

        if first_divergence:
            break

    # Report results
    if first_divergence:
        FAIL_COUNT += 1
        div = first_divergence
        # Classify the error type for guided diagnosis
        if div["actual"] == 0 and div["expected"] != 0:
            bug_type = "A (computation=0, expected non-zero → logic error or missing init)"
        else:
            diff = div["actual"] ^ div["expected"]
            bug_type = f"A (data-wrong, xor_diff=0x{diff:x} → logic or init error)"

        msg = (
            f"[LAYERED] FIRST DIVERGENCE at cycle={div['cycle']} "
            f"signal={div['signal']}: "
            f"expected=0x{div['expected']:08x} got=0x{div['actual']:08x} "
            f"→ bug_type={bug_type}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(
            f"[LAYERED] PASS — {cycles_checked} cycles matched golden model"
        )


@cocotb.test()
async def test_internal_signals(dut):
    """Test 4: Per-cycle internal signal comparison against golden model.

    This is the KEY verification test. It reads RTL internal registers
    via VPI (cocotb's dut.hierarchy.signal.value) and compares them
    against golden model trace on EVERY clock cycle.

    This test catches bugs that test_layered misses — test_layered only
    compares top-level output ports, missing internal register divergences.

    Timing semantics:
        After `await RisingEdge(dut.clk)`, cocotb + iverilog VPI reads values
        produced by the PREVIOUS posedge's NBA. Equivalently: at RisingEdge N,
        cocotb sees POST-NBA of posedge N-1.

        The golden model trace records POST-NBA state for each posedge.
        The comparison loop reads trace[N] at RisingEdge N, which shows the
        results of posedge N-1's computation.

        Registered outputs (ready, valid, etc.) have a 1-cycle delay from
        the FSM state that computes them — the golden model must track this.

        The comparison alignment is controlled by DRIVE_PHASE_CYCLES. With
        DRIVE_PHASE_CYCLES=0 and block_trace starting after cycle 0 (reset),
        the first comparison RisingEdge reads POST-NBA of the drive posedge,
        matching golden cycle 1 (IDLE->active transition).

    Skipped if golden_model.py trace mode does not include internal signals.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    expected_cycles = golden_run()
    if not expected_cycles:
        dut._log.info("[SKIP] test_internal_signals: golden model returned no data")
        return

    # Check if trace includes internal signals (any key containing '.' or ending with _reg)
    has_internal = any('.' in k or k.endswith('_reg')
                       for entry in expected_cycles for k in entry.keys())
    if not has_internal:
        dut._log.info("[SKIP] test_internal_signals: golden trace has no internal signals")
        return

    dut._log.info(f"[INTERNAL] Comparing {len(expected_cycles)} cycles, internal signals")

    # IMPORTANT: codegen MUST add drive_inputs() call here with golden model
    # test vectors before the comparison loop. Without driving inputs,
    # the DUT has no stimulus and all outputs will be initial values.
    # <CODEGEN: drive_inputs() call here — same as test_layered>

    first_divergence = None
    cycles_checked = 0
    signals_compared = 0
    signals_not_found = []

    # Align golden trace with DUT state after drive phase.
    # drive_inputs() consumes 1 RisingEdge that reads POST-NBA of the previous
    # posedge (before drive). The comparison loop's first RisingEdge reads
    # POST-NBA of the drive posedge. With block_trace starting after cycle 0
    # (reset), DRIVE_PHASE_CYCLES=0 aligns correctly.
    # For designs where codegen uses the full trace (including cycle 0),
    # set DRIVE_PHASE_CYCLES=1.
    compare_cycles = expected_cycles[DRIVE_PHASE_CYCLES:]

    for cycle_idx, expected in enumerate(compare_cycles):
        await RisingEdge(dut.clk)

        for sig_name, expected_val in expected.items():
            if sig_name in INPUT_PORTS:
                continue  # skip input signals

            try:
                # Navigate VPI hierarchy for module-qualified names
                if '.' in sig_name:
                    parts = sig_name.split('.')
                    obj = dut
                    for part in parts:
                        # Handle array indexing: w_reg[0] → w_reg, index 0
                        if '[' in part:
                            arr_name, idx_str = part.split('[')
                            idx = int(idx_str.rstrip(']'))
                            obj = getattr(obj, arr_name)
                            obj = obj[idx]
                        else:
                            obj = getattr(obj, part)
                    actual_val = int(obj.value)
                else:
                    sig = getattr(dut, sig_name)
                    actual_val = int(sig.value)

                signals_compared += 1
                if actual_val != expected_val:
                    first_divergence = {
                        "cycle": cycle_idx,
                        "signal": sig_name,
                        "expected": expected_val,
                        "actual": actual_val,
                    }
                    break
            except (AttributeError, IndexError):
                if sig_name not in signals_not_found:
                    signals_not_found.append(sig_name)

        cycles_checked += 1
        if first_divergence:
            break

    # Warn about signals that could not be found in DUT hierarchy
    if signals_not_found:
        dut._log.warning(
            f"[INTERNAL] {len(signals_not_found)} signal(s) not found in DUT, "
            f"skipped: {signals_not_found[:5]}"
            + (f" ... and {len(signals_not_found)-5} more" if len(signals_not_found) > 5 else "")
        )

    # Safety check: verify we actually compared something
    if signals_compared == 0:
        dut._log.error(
            "[INTERNAL] ZERO signals were compared — golden trace signal names "
            "do not match any DUT signals. Check naming convention."
        )
        raise AssertionError(
            "test_internal_signals: no signals compared, trace names may not match RTL"
        )

    if first_divergence:
        FAIL_COUNT += 1
        div = first_divergence
        msg = (
            f"[INTERNAL] FIRST DIVERGENCE at cycle={div['cycle']} "
            f"signal={div['signal']}: "
            f"expected=0x{div['expected']:08x} got=0x{div['actual']:08x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(
            f"[INTERNAL] PASS — {cycles_checked} cycles, "
            f"{signals_compared} signal values matched"
        )


@cocotb.test()
async def test_summary(dut):
    """Final test: reports overall pass/fail."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await RisingEdge(dut.clk)
    if FAIL_COUNT == 0:
        dut._log.info("ALL TESTS PASSED")
    else:
        dut._log.error(f"FAILED: {FAIL_COUNT} assertion(s) failed")
