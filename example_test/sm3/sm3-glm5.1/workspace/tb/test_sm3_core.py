@cocotb.test()
async def test_internal_signals(dut):
    """Test 4: Per-cycle internal register comparison against golden model.

    This is the KEY verification test. It drives the 'abc' test vector and
    compares DUT internal registers (A~H, V0~V7, w_j, w_prime_j, round_cnt,
    fsm control signals) against the golden model trace on EVERY clock cycle.

    Timing alignment:
      - Golden model trace[0] = T1 (LOAD): register state after load_en NBA.
      - cocotb RisingEdge after drive_block() reads POST-NBA of the drive posedge.
      - DRIVE_PHASE_CYCLES=1 offsets the golden trace to account for the 1-cycle
        registered control signal delay.
      - First compared RisingEdge corresponds to golden trace[0] (T1 LOAD).

    Reports FIRST DIVERGENCE with cycle, signal name, expected vs actual.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Get golden model trace for test vector 0 ("abc")
    expected_cycles = golden_run(test_vector_index=0)
    if not expected_cycles:
        dut._log.info("[SKIP] test_internal_signals: golden model returned no data")
        return

    dut._log.info(
        f"[INTERNAL] Golden model trace has {len(expected_cycles)} cycles"
    )

    # Drive the "abc" block
    tv = TEST_VECTORS[0]
    msg_block_val = _parse_msg_block(tv["inputs"]["msg_block"])
    await drive_block(dut, msg_block_val, is_last_val=1)

    # Map golden trace signal names to DUT hierarchical paths.
    # The golden model uses flat names; the RTL uses hierarchy under sm3_core.
    GOLDEN_TO_DUT = {
        # FSM control signals (top-level outputs)
        "load_en":     "load_en",
        "calc_en":     "calc_en",
        "update_v_en": "update_v_en",
        "ready":       "ready",
        "hash_valid":  "hash_valid",
        "round_cnt":   "round_cnt",
        # Compression working registers (inside sm3_compress)
        "A": "u_compress.A",
        "B": "u_compress.B",
        "C": "u_compress.C",
        "D": "u_compress.D",
        "E": "u_compress.E",
        "F": "u_compress.F",
        "G": "u_compress.G",
        "H": "u_compress.H",
        # V registers (inside sm3_compress)
        "V0": "u_compress.V0",
        "V1": "u_compress.V1",
        "V2": "u_compress.V2",
        "V3": "u_compress.V3",
        "V4": "u_compress.V4",
        "V5": "u_compress.V5",
        "V6": "u_compress.V6",
        "V7": "u_compress.V7",
        # W-gen outputs
        "w_j":      "u_w_gen.w_j",
        "w_prime_j": "u_w_gen.w_prime_j",
    }

    first_divergence = None
    cycles_checked = 0
    signals_compared = 0
    signals_not_found = []

    # Align: golden trace starts at T1 (LOAD).
    # drive_block consumed HOLD_CYCLES RisingEdges after msg_valid assertion.
    # With DRIVE_PHASE_CYCLES=1, we skip the first entry to align.
    compare_cycles = expected_cycles[DRIVE_PHASE_CYCLES:]

    for cycle_idx, expected in enumerate(compare_cycles):
        await RisingEdge(dut.clk)

        for sig_name, expected_val in expected.items():
            # Skip input signals and intermediate debug signals
            if sig_name in ("fsm_state", "SS1", "SS2", "TT1", "TT2", "hash_out"):
                continue

            dut_path = GOLDEN_TO_DUT.get(sig_name)
            if dut_path is None:
                continue

            try:
                # Navigate VPI hierarchy for dotted paths
                if '.' in dut_path:
                    parts = dut_path.split('.')
                    obj = dut
                    for part in parts:
                        obj = getattr(obj, part)
                    actual_val = int(obj.value)
                else:
                    sig = getattr(dut, dut_path)
                    actual_val = int(sig.value)

                # Mask to appropriate width
                if sig_name == "round_cnt":
                    actual_val &= 0x3F  # 6-bit
                elif sig_name in ("load_en", "calc_en", "update_v_en",
                                  "ready", "hash_valid"):
                    actual_val &= 1
                elif sig_name.startswith("V") or sig_name in (
                    "A", "B", "C", "D", "E", "F", "G", "H",
                    "w_j", "w_prime_j",
                ):
                    actual_val &= MASK32

                signals_compared += 1
                if actual_val != expected_val:
                    first_divergence = {
                        "cycle": cycle_idx,
                        "signal": sig_name,
                        "dut_path": dut_path,
                        "expected": expected_val,
                        "actual": actual_val,
                    }
                    break
            except (AttributeError, IndexError):
                if sig_name not in signals_not_found:
                    signals_not_found.append(sig_name)

        cycles_checked += 1
        if first_divergence:
            break

    # Report signals not found
    if signals_not_found:
        dut._log.warning(
            f"[INTERNAL] {len(signals_not_found)} signal(s) not found in DUT, "
            f"skipped: {signals_not_found[:5]}"
            + (f" ... and {len(signals_not_found)-5} more"
               if len(signals_not_found) > 5 else "")
        )

    if signals_compared == 0:
        dut._log.error(
            "[INTERNAL] ZERO signals compared -- golden trace signal names "
            "do not match any DUT signals. Check naming / hierarchy."
        )
        raise AssertionError(
            "test_internal_signals: no signals compared, check RTL hierarchy names"
        )

    if first_divergence:
        FAIL_COUNT += 1
        div = first_divergence
        msg = (
            f"[INTERNAL] FIRST DIVERGENCE at cycle={div['cycle']} "
            f"signal={div['signal']} (path={div['dut_path']}): "
            f"expected=0x{div['expected']:08x} got=0x{div['actual']:08x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)
    else:
        dut._log.info(
            f"[INTERNAL] PASS -- {cycles_checked} cycles, "
            f"{signals_compared} signal values matched golden model"
        )


@cocotb.test()
async def test_summary(dut):
    """Final test: reports overall pass/fail."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await RisingEdge(dut.clk)
    if FAIL_COUNT == 0:
        dut._log.info("ALL TESTS PASSED")
    else:
        dut._log.error(f"FAILED: {FAIL_COUNT} assertion(s) failed")
