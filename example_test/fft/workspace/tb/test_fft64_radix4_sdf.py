@cocotb.test()
async def test_reset(dut):
    """Test 1: Reset behavior -- outputs quiescent after async active-low reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset: verify all output ports are 0
    reset_ok = True
    for port_name in OUTPUT_PORTS:
        try:
            sig = getattr(dut, port_name)
            val = int(sig.value)
            if val != 0:
                FAIL_COUNT += 1
                dut._log.error(f"[RESET] {port_name}={val} after reset, expected 0")
                reset_ok = False
        except AttributeError:
            dut._log.warning(f"[RESET] Port {port_name} not found in DUT")

    if reset_ok:
        dut._log.info("[PASS] reset behavior correct")


@cocotb.test()
async def test_protocol(dut):
    """Test 2: Protocol compliance -- valid handshake behavior.

    Checks:
    1. Reset clears out_valid
    2. After driving 64 samples, out_valid eventually asserts
    3. out_valid is high for exactly 64 consecutive cycles
    """
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # Check 1: After reset, out_valid must be 0
    for valid_name in VALID_OUTPUT_PORTS:
        try:
            sig = getattr(dut, valid_name)
            val = int(sig.value)
            if val != 0:
                FAIL_COUNT += 1
                dut._log.error(
                    f"[PROTOCOL] {valid_name}={val} after reset, expected 0"
                )
        except AttributeError:
            pass

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_protocol: golden_model.py not available")
        return

    # Drive impulse test vector for protocol check
    tv = golden_get_test_vectors()[0]  # impulse_idx0
    samples_re = tv["inputs"]["samples_re"]
    samples_im = tv["inputs"]["samples_im"]

    await drive_fft_inputs(dut, samples_re, samples_im)

    # Wait for pipeline latency
    for _ in range(PIPELINE_LATENCY):
        await RisingEdge(dut.clk)

    # Check 2: out_valid should assert after pipeline latency
    valid_count = 0
    for _ in range(N + 20):
        await RisingEdge(dut.clk)
        if int(dut.out_valid.value) == 1:
            valid_count += 1

    if valid_count < N:
        dut._log.error(
            f"[PROTOCOL] Only {valid_count}/{N} valid outputs after pipeline latency"
        )
        FAIL_COUNT += 1
    else:
        dut._log.info(
            f"[PASS] protocol: {valid_count} valid outputs captured"
        )

    if FAIL_COUNT == 0:
        dut._log.info("[PASS] protocol compliance checks passed")


@cocotb.test()
async def test_layered(dut):
    """Test 3: Layered verification -- golden model output comparison.

    For the FFT streaming pipeline design:
    1. Drive all 64 input samples from each golden model test vector
    2. Wait for pipeline latency
    3. Capture all 64 output samples
    4. Compare against golden_model.compute() expected output
    5. Report SQNR and max error per test vector

    Skipped if golden_model.py is not available.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_layered: golden_model.py not available")
        return

    await ensure_clock(dut)

    test_vectors = golden_get_test_vectors()
    dut._log.info(f"[LAYERED] Running {len(test_vectors)} test vectors")

    for vec_idx, tv in enumerate(test_vectors):
        test_name = f"layered_{tv['name']}"

        # Reset before each test vector
        await reset_dut(dut)

        # Get expected output from golden model
        expected = golden_compute(tv["inputs"], trace=False)
        expected_re = expected["samples_re"]
        expected_im = expected["samples_im"]

        # Drive input samples
        samples_re = tv["inputs"]["samples_re"]
        samples_im = tv["inputs"]["samples_im"]
        await drive_fft_inputs(dut, samples_re, samples_im)

        # Capture outputs
        actual_outputs = await capture_fft_outputs(dut)

        # Compare
        compare_fft_results(
            dut, actual_outputs,
            expected_re, expected_im,
            test_name,
        )


@cocotb.test()
async def test_internal_signals(dut):
    """Test 4: Per-cycle internal signal comparison against golden model.

    For the FFT design, the golden model trace reports pipeline stage
    completions but does not provide per-cycle internal register values.
    This test verifies:
    1. Pipeline stage progression by checking out_valid timing
    2. Output index sequence follows radix-4 digit-reversed order
    3. Output data stability between posedge and negedge (timing contract)

    Skipped if golden_model.py is not available.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)

    # Use impulse test vector for detailed analysis
    tv = golden_get_test_vectors()[0]
    await reset_dut(dut)

    samples_re = tv["inputs"]["samples_re"]
    samples_im = tv["inputs"]["samples_im"]
    await drive_fft_inputs(dut, samples_re, samples_im)

    # --- Timing contract check ---
    # Verify out_valid first assertion at cycle T+67 (within +/- 1 cycle tolerance)
    dut._log.info("[INTERNAL] Checking pipeline latency timing")
    found_valid = False
    first_valid_cycle = 0
    for cyc in range(PIPELINE_LATENCY + 20):
        await RisingEdge(dut.clk)
        if int(dut.out_valid.value) == 1:
            if not found_valid:
                first_valid_cycle = cyc
                found_valid = True
                break

    if not found_valid:
        dut._log.error(
            f"[INTERNAL] out_valid never asserted within "
            f"{PIPELINE_LATENCY + 20} cycles of stimulus"
        )
        FAIL_COUNT += 1
        return

    latency_offset = abs(first_valid_cycle - PIPELINE_LATENCY)
    if latency_offset > 2:
        dut._log.warning(
            f"[INTERNAL] Pipeline latency offset={latency_offset} cycles "
            f"(expected ~{PIPELINE_LATENCY}, got {first_valid_cycle})"
        )
    else:
        dut._log.info(
            f"[INTERNAL] Pipeline latency: first out_valid at cycle {first_valid_cycle} "
            f"(expected ~{PIPELINE_LATENCY}, delta={latency_offset})"
        )

    # --- Registered output stability check ---
    # Verify out_data_re/im don't change between posedge and negedge when out_valid=1
    dut._log.info("[INTERNAL] Checking registered output stability")
    violations = []

    # Reset and re-drive for stability check
    await reset_dut(dut)
    await drive_fft_inputs(dut, samples_re, samples_im)

    for _ in range(PIPELINE_LATENCY):
        await RisingEdge(dut.clk)

    for cyc in range(N + 10):
        await RisingEdge(dut.clk)
        if int(dut.out_valid.value) == 1:
            posedge_re = int(dut.out_data_re.value)
            posedge_im = int(dut.out_data_im.value)
            posedge_idx = int(dut.out_index.value)

            await FallingEdge(dut.clk)
            negedge_re = int(dut.out_data_re.value)
            negedge_im = int(dut.out_data_im.value)
            negedge_idx = int(dut.out_index.value)

            if posedge_re != negedge_re:
                violations.append(
                    f"cycle={cyc} out_data_re: posedge={posedge_re} negedge={negedge_re}"
                )
            if posedge_im != negedge_im:
                violations.append(
                    f"cycle={cyc} out_data_im: posedge={posedge_im} negedge={negedge_im}"
                )
            if posedge_idx != negedge_idx:
                violations.append(
                    f"cycle={cyc} out_index: posedge={posedge_idx} negedge={negedge_idx}"
                )

            if len(violations) >= MAX_COLLECTED_DIVERGENCES:
                break

    if violations:
        dut._log.error(
            f"[INTERNAL] {len(violations)} registered output stability violation(s):"
        )
        for v in violations[:5]:
            dut._log.error(f"  {v}")
        FAIL_COUNT += len(violations)
    else:
        dut._log.info("[INTERNAL] PASS -- registered output stability verified")

    # Stimulus sanity check
    if INPUT_PORTS and not _STIMULUS_DELIVERED:
        raise AssertionError(
            "test_internal_signals: drive_fft_inputs() was never called "
            "(no stimulus delivered)"
        )

    if not violations:
        dut._log.info("[INTERNAL] PASS -- internal signal checks passed")


@cocotb.test()
async def test_timing_contract(dut):
    """Test 5: Verify timing contract invariants at runtime.

    Checks:
    1. Registered outputs (out_valid, out_data_re, out_data_im, out_index)
       are stable between posedge and negedge -- they must be NBA-driven.
    2. Pipeline delay from first in_valid to first out_valid matches
       spec.json timing_contract.pipeline_delay_cycles (67 cycles).
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_timing_contract: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Drive impulse vector for timing measurement
    tv = golden_get_test_vectors()[0]
    await drive_fft_inputs(dut, tv["inputs"]["samples_re"], tv["inputs"]["samples_im"])

    REGISTERED_OUTPUTS = ["out_valid", "out_data_re", "out_data_im", "out_index"]
    PIPELINE_DELAY_CYCLES = 67

    violations = []
    first_in_valid_cycle = None
    first_out_valid_cycle = None
    cycle = 0

    for cycle in range(PIPELINE_DELAY_CYCLES + N + 20):
        await RisingEdge(dut.clk)
        cycle += 1

        # Track first in_valid assertion (already driven by drive_fft_inputs,
        # but check from this test's perspective)
        in_v = int(dut.in_valid.value)
        if in_v == 1 and first_in_valid_cycle is None:
            first_in_valid_cycle = cycle

        # Track first out_valid assertion
        out_v = int(dut.out_valid.value)
        if out_v == 1 and first_out_valid_cycle is None:
            first_out_valid_cycle = cycle

            # Measure pipeline latency
            if first_in_valid_cycle is not None:
                measured_latency = first_out_valid_cycle - first_in_valid_cycle
                if abs(measured_latency - PIPELINE_DELAY_CYCLES) > 2:
                    violations.append(
                        f"Pipeline latency: measured={measured_latency} "
                        f"expected={PIPELINE_DELAY_CYCLES} "
                        f"delta={abs(measured_latency - PIPELINE_DELAY_CYCLES)}"
                    )

        # Registered output stability check when out_valid is high
        if out_v == 1:
            for port_name in REGISTERED_OUTPUTS:
                try:
                    sig = getattr(dut, port_name)
                    posedge_val = int(sig.value)
                    await FallingEdge(dut.clk)
                    negedge_val = int(sig.value)
                    if posedge_val != negedge_val:
                        violations.append(
                            f"cycle={cycle} {port_name}: changed between "
                            f"posedge ({posedge_val}) and negedge ({negedge_val}) "
                            f"-- registered output must be stable"
                        )
                    await RisingEdge(dut.clk)
                    # Note: we advanced one extra cycle for the FallingEdge/RisingEdge
                    # This is acceptable for a sampling-based check
                    break  # only check first few valid outputs
                except AttributeError:
                    pass
            if violations:
                break  # fail-fast on first timing violation

        if first_out_valid_cycle is not None and cycle > first_out_valid_cycle + N:
            break

    if violations:
        dut._log.error(f"[TIMING CONTRACT] {len(violations)} violation(s):")
        for v in violations[:5]:
            dut._log.error(f"  {v}")
        FAIL_COUNT += 1
        raise AssertionError(
            f"Timing contract violated: {violations[0]}"
        )
    else:
        dut._log.info("[TIMING CONTRACT] PASS -- all invariants hold")


@cocotb.test()
async def test_summary(dut):
    """Final test: reports overall pass/fail.

    MUST be the last test defined in this file -- cocotb runs tests in
    definition order. This test reads the accumulated FAIL_COUNT.
    """
    global FAIL_COUNT
    await ensure_clock(dut)
    await RisingEdge(dut.clk)
    if FAIL_COUNT == 0:
        dut._log.info("ALL TESTS PASSED")
    else:
        dut._log.error(f"FAILED: {FAIL_COUNT} assertion(s) failed")
