async def drive_inputs(dut, input_values, valid_name='valid_in',
                       ready_name='ready_out'):
    """Wait for ready, then drive valid=1 with input values.

    Uses VPI-safe 2-RisingEdge timing:
    1. Wait for ready=1
    2. Drive ALL inputs simultaneously (valid + data)
    3. Wait TWO RisingEdges — first for VPI propagation, second for DUT sampling
    4. Deassert inputs

    This pattern is required because cocotb + iverilog VPI can delay
    wide signal writes. The 2-edge wait ensures the DUT
    sees all inputs correctly before we deassert.

    Args:
        dut: cocotb DUT handle
        input_values: dict mapping port names to integer values
        valid_name: name of the valid input port
        ready_name: name of the ready/ack output port
    """
    # Wait for ready
    timeout = TIMEOUT_CYCLES
    ready_sig = getattr(dut, ready_name)
    while int(ready_sig.value) != 1 and timeout > 0:
        await RisingEdge(dut.clk)
        timeout -= 1
    assert timeout > 0, "Timeout waiting for ready before drive_inputs"

    # Drive all inputs at once
    for port_name, value in input_values.items():
        getattr(dut, port_name).value = value
    getattr(dut, valid_name).value = 1

    # Wait two rising edges — first for VPI propagation, second for DUT sampling
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)

    # Deassert after DUT has sampled
    getattr(dut, valid_name).value = 0
    for port_name in input_values:
        getattr(dut, port_name).value = 0


async def wait_valid(dut, valid_name='valid_out', max_cycles=200):
    """Poll for valid=1, return cycle count waited."""
    valid_sig = getattr(dut, valid_name)
    for i in range(max_cycles):
        await RisingEdge(dut.clk)
        if int(valid_sig.value) == 1:
            return i + 1
    return -1  # timeout


