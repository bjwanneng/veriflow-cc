@cocotb.test()
async def test_reset(dut):
    """Test 1: Reset behavior -- outputs quiescent after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    reset_ok = True
    # After reset, valid outputs should be 0
    for port_name in ["s_axi_awready", "s_axi_wready", "s_axi_bvalid",
                       "s_axi_arready", "s_axi_rvalid",
                       "m_axi_awvalid", "m_axi_wvalid", "m_axi_arvalid",
                       "m_axi_bready", "m_axi_rready"]:
        try:
            sig = getattr(dut, port_name)
            val = int(sig.value)
            if val != 0:
                FAIL_COUNT += 1
                dut._log.error(f"[RESET] {port_name}={val} after reset, expected 0")
                reset_ok = False
        except AttributeError:
            dut._log.warning(f"[RESET] Port {port_name} not found in DUT")

    if reset_ok:
        dut._log.info("[PASS] reset behavior correct")


