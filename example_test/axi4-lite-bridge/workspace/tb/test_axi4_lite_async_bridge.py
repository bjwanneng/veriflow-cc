def _format_txn_mismatch(test_name, txn_idx, field, expected, actual, width=32):
    """Format a transaction-level mismatch with full hex and diagnostic data."""
    hex_digits = max((width + 3) // 4, 1)
    return (
        f"[FAIL] test={test_name} txn={txn_idx} signal={field} "
        f"(width={width}b):\n"
        f"  expected = 0x{expected:0{hex_digits}x}\n"
        f"  actual   = 0x{actual:0{hex_digits}x}\n"
        f"  xor diff = 0x{expected ^ actual:0{hex_digits}x}"
    )


