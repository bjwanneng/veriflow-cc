"""cocotb integration testbench for sm3_core -- golden model cross-check.
Generated by VeriFlow-CC.

Timing convention (IMPORTANT):
  cocotb + iverilog via VPI: after `await RisingEdge(dut.clk)`, cocotb reads
  values produced by the PREVIOUS posedge's NBA. Equivalently: at RisingEdge N,
  cocotb sees POST-NBA of posedge N-1 = PRE-NBA of posedge N.

  The golden model trace records the register state AFTER each posedge's NBA.
  The cocotb comparison loop reads each trace entry at the NEXT RisingEdge.

  Practical effect: the golden model records the NEW register values after each
  computation step. The comparison is aligned by DRIVE_PHASE_CYCLES offset.

  Registered outputs (ready, hash_valid, etc.) have a 1-cycle delay: they
  reflect the combinational _next signal from the PREVIOUS posedge's state.

Clock management (IMPORTANT):
  In cocotb v2.0+, background tasks (including Clock) are killed between tests.
  Each test MUST call ensure_clock() to start a fresh clock instance.
  Do NOT use a global _CLOCK_STARTED flag.
"""

import os, sys
from pathlib import Path
import cocotb
from cocotb.clock import Clock
from cocotb.triggers import RisingEdge, ClockCycles, Timer

CLK_PERIOD_NS = 10  # 10ns period (100 MHz), well within 150 MHz target
TIMEOUT_CYCLES = 500
FAIL_COUNT = 0

# --- Golden model import ---------------------------------------------------
GOLDEN_AVAILABLE = False
GM_PATH = Path(__file__).parent.parent / "docs" / "golden_model.py"
if GM_PATH.exists():
    sys.path.insert(0, str(GM_PATH.parent))
    try:
        import golden_model
        from golden_model import run as golden_run, TEST_VECTORS, compute
        GOLDEN_AVAILABLE = True
    except ImportError:
        pass

# --- Port configuration (from spec.json) -----------------------------------
INPUT_PORTS = {
    "msg_valid": 1,
    "msg_block": 512,
    "is_last":   1,
}
OUTPUT_PORTS = {
    "ready":      1,
    "hash_valid": 1,
    "hash_out":  256,
}
VALID_OUTPUT_PORTS = ["hash_valid"]
HANDSHAKE_PORTS = {"msg_valid": "ready"}
HOLD_UNTIL_ACK_PORTS = []

# DRIVE_PHASE_CYCLES from spec.json module_connectivity timing_contract:
#   max pipeline_delay_cycles across all registered control paths = 1
#   (load_en, calc_en, update_v_en all have pipeline_delay_cycles=1)
DRIVE_PHASE_CYCLES = 1

# HOLD_CYCLES: msg_block and is_last must be held stable for
#   max_pipeline_delay + 1 = 2 cycles after msg_valid assertion.
HOLD_CYCLES = 2

MASK32 = 0xFFFFFFFF
MASK256 = (1 << 256) - 1


# --- Clock management ------------------------------------------------------
async def ensure_clock(dut):
    """Start clock. Must be called at the start of every test."""
    clock = Clock(dut.clk, CLK_PERIOD_NS, unit="ns")
    cocotb.start_soon(clock.start())
    await RisingEdge(dut.clk)


# --- Helpers ---------------------------------------------------------------

async def reset_dut(dut):
    """Apply async-active-low reset and wait for outputs to settle."""
    global FAIL_COUNT
    dut.rst_n.value = 0
    dut.msg_valid.value = 0
    dut.msg_block.value = 0
    dut.is_last.value = 0
    await ClockCycles(dut.clk, 5)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut._log.info("[RESET] rst_n released, DUT in IDLE")


async def drive_block(dut, msg_block_val, is_last_val):
    """Drive a single message block with proper hold time.

    Protocol:
      1. Wait for ready=1
      2. Assert msg_valid, msg_block, is_last simultaneously
      3. Wait 1 RisingEdge (DUT samples handshake)
      4. Deassert msg_valid
      5. Hold msg_block and is_last stable for HOLD_CYCLES total after msg_valid assertion
    """
    # Wait for ready
    for _ in range(TIMEOUT_CYCLES):
        if int(dut.ready.value) == 1:
            break
        await RisingEdge(dut.clk)
    else:
        raise AssertionError("Timeout waiting for ready before drive_block")

    # Drive msg_valid + msg_block + is_last
    dut.msg_block.value = msg_block_val
    dut.msg_valid.value = 1
    dut.is_last.value = is_last_val
    await RisingEdge(dut.clk)  # DUT samples inputs at this posedge

    # Deassert msg_valid but HOLD msg_block and is_last stable
    dut.msg_valid.value = 0
    # Hold data stable for (HOLD_CYCLES - 1) more cycles (already 1 cycle after assertion)
    for _ in range(HOLD_CYCLES - 1):
        await RisingEdge(dut.clk)

    # Now safe to clear data inputs
    dut.msg_block.value = 0
    dut.is_last.value = 0


async def wait_hash_valid(dut, max_cycles=200):
    """Poll for hash_valid=1, return cycle count waited."""
    for i in range(max_cycles):
        await RisingEdge(dut.clk)
        if int(dut.hash_valid.value) == 1:
            return i + 1
    return -1  # timeout


def _parse_msg_block(val):
    """Convert msg_block from hex string or int to 512-bit integer."""
    if isinstance(val, str):
        return int(val, 16)
    return val & ((1 << 512) - 1)


