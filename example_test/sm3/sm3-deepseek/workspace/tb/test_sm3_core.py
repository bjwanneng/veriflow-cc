@cocotb.test()
async def test_reset(dut):
    """Test 1: Reset behavior — outputs quiescent after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    reset_ok = True
    # After reset: hash_valid must be 0
    try:
        val = int(dut.hash_valid.value)
        if val != 0:
            FAIL_COUNT += 1
            dut._log.error(f"[RESET] hash_valid={val} after reset, expected 0")
            reset_ok = False
    except Exception:
        pass

    # After reset: ready should be 1 (FSM is in IDLE)
    try:
        val = int(dut.ready.value)
        if val != 1:
            FAIL_COUNT += 1
            dut._log.error(f"[RESET] ready={val} after reset, expected 1")
            reset_ok = False
    except Exception:
        pass

    if reset_ok:
        dut._log.info("[PASS] reset behavior correct")


