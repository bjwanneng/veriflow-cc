@cocotb.test()
async def test_reset(dut):
    """Test 1: Reset behavior -- outputs quiescent after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset: verify all output ports are 0
    reset_ok = True
    for port_name in OUTPUT_PORTS:
        try:
            sig = getattr(dut, port_name)
            val = int(sig.value)
            if val != 0:
                FAIL_COUNT += 1
                dut._log.error(f"[RESET] {port_name}={val} after reset, expected 0")
                reset_ok = False
        except AttributeError:
            dut._log.warning(f"[RESET] Port {port_name} not found in DUT")

    if reset_ok:
        dut._log.info("[PASS] reset behavior correct")


@cocotb.test()
async def test_protocol(dut):
    """Test 2: Protocol compliance -- valid behavior after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset, valid must be 0
    for valid_name in VALID_OUTPUT_PORTS:
        try:
            sig = getattr(dut, valid_name)
            val = int(sig.value)
            if val != 0:
                FAIL_COUNT += 1
                dut._log.error(
                    f"[PROTOCOL] {valid_name}={val} after reset, expected 0"
                )
        except AttributeError:
            pass

    if FAIL_COUNT == 0:
        dut._log.info("[PASS] protocol compliance checks passed")


@cocotb.test()
async def test_layered(dut):
    """Test 3: Layered verification -- golden model output port comparison.

    Drives all 4 test vectors, waits for valid, compares data_out against
    golden model expected output.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_layered: golden_model.py not available")
        return

    await ensure_clock(dut)

    test_vectors = get_test_vectors()

    for vec_idx, vec in enumerate(test_vectors):
        await reset_dut(dut)

        # Drive inputs from test vector
        await drive_inputs(dut, vec["inputs"], valid_name="start")

        # Wait for valid output
        waited = await wait_valid(dut, valid_name="valid", max_cycles=TIMEOUT_CYCLES)
        assert waited >= 0, (
            f"[LAYERED] test vector {vec_idx} ({vec['name']}): "
            f"timed out waiting for valid=1"
        )

        # Sample registered output at next RisingEdge (post-NBA)
        await RisingEdge(dut.clk)

        # Compare data_out
        expected_data = vec["expected"]["data_out"]
        actual_data = int(dut.data_out.value)
        width = 128
        hex_digits = width // 4  # 32 hex digits for 128-bit

        if actual_data != expected_data:
            FAIL_COUNT += 1
            dut._log.error(
                f"[LAYERED] FIRST DIVERGENCE at vector={vec_idx} ({vec['name']}) "
                f"signal=data_out (width={width}b):\n"
                f"  expected = 0x{expected_data:0{hex_digits}x}\n"
                f"  actual   = 0x{actual_data:0{hex_digits}x}\n"
                f"  xor diff = 0x{expected_data ^ actual_data:0{hex_digits}x}"
            )
            raise AssertionError(
                f"[LAYERED] data_out mismatch on vector {vec_idx} ({vec['name']})"
            )
        else:
            dut._log.info(
                f"[PASS] vector={vec_idx} ({vec['name']}) "
                f"data_out=0x{actual_data:0{hex_digits}x}"
            )

    dut._log.info(
        f"[LAYERED] PASS -- {len(test_vectors)} test vectors matched"
    )


@cocotb.test()
async def test_internal_signals(dut):
    """Test 4: Per-cycle internal signal comparison against golden model.

    Compares state_reg, round_counter_reg, data_out_reg, valid_reg
    against golden model trace on every clock cycle via VPI.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Run golden model with trace for default test vector (index 0)
    expected_cycles = golden_run(test_vector_index=0)
    if not expected_cycles:
        dut._log.info("[SKIP] test_internal_signals: golden model returned no data")
        return

    dut._log.info(
        f"[INTERNAL] Comparing {len(expected_cycles)} cycles against golden model"
    )

    # Pre-validate golden trace signal names against DUT hierarchy
    _validate_trace_signal_names(expected_cycles, dut, "test_internal_signals")

    # Drive inputs from default test vector (index 0)
    tv0 = TEST_VECTORS[0]
    await drive_inputs(dut, tv0["inputs"], valid_name="start")

    # Golden trace signal -> DUT signal mapping (internal registers)
    GOLDEN_TO_PORT = {
        "state_reg": "state_reg",
        "round_counter_reg": "round_counter_reg",
        "data_out_reg": "data_out_reg",
        "valid_reg": "valid_reg",
    }

    divergences = []
    cycles_checked = 0
    signals_compared = 0
    signals_not_found = []

    # Alignment: skip golden cycle 0 (reset state).
    compare_cycles = _align_trace(expected_cycles)

    for cycle_idx, expected in enumerate(compare_cycles):
        stop_outer = False
        for sig_name, expected_val in expected.items():
            # Map golden trace name to DUT signal name
            dut_name = GOLDEN_TO_PORT.get(sig_name)
            if dut_name is None:
                # Not a mapped internal signal -- skip
                continue

            try:
                sig = getattr(dut, dut_name)
                actual_val = int(sig.value)
                signals_compared += 1
                if actual_val != expected_val:
                    try:
                        sig_width = len(sig)
                    except Exception:
                        sig_width = _lookup_signal_width(sig_name)
                    if _record_divergence(
                        divergences, sig_name, expected_val, actual_val,
                        cycle_idx, sig_width,
                    ):
                        stop_outer = True
                        break
            except (AttributeError, IndexError):
                if dut_name not in signals_not_found:
                    signals_not_found.append(dut_name)

        cycles_checked += 1

        if stop_outer:
            break

        # Advance DUT one cycle for the NEXT golden entry.
        await RisingEdge(dut.clk)

    # Stimulus sanity check
    if INPUT_PORTS and not _STIMULUS_DELIVERED:
        raise AssertionError(
            "test_internal_signals: drive_inputs() was never called (no stimulus delivered)"
        )

    # Warn about signals that could not be found
    if signals_not_found:
        dut._log.warning(
            f"[INTERNAL] {len(signals_not_found)} signal(s) not found in DUT, "
            f"skipped: {signals_not_found[:5]}"
        )

    # Safety check
    if signals_compared == 0:
        dut._log.error(
            "[INTERNAL] ZERO signals were compared -- golden trace signal names "
            "do not match any DUT signals. Check naming convention."
        )
        raise AssertionError(
            "test_internal_signals: no signals compared, trace names may not match RTL"
        )

    if divergences:
        FAIL_COUNT += len(divergences)
        _finalize_divergences(dut, divergences, "INTERNAL")
    else:
        dut._log.info(
            f"[INTERNAL] PASS -- {cycles_checked} cycles, "
            f"{signals_compared} signal values matched"
        )


@cocotb.test()
async def test_timing_contract(dut):
    """Test: Verify timing contract invariants at runtime.

    Checks that registered outputs (data_out, valid) remain stable between
    posedge and negedge -- they must not change combinationally.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_timing_contract: golden_model.py not available")
        return

    await ensure_clock(dut)
    await reset_dut(dut)

    # Drive stimulus from test vector 0
    tv0 = TEST_VECTORS[0]
    await drive_inputs(dut, tv0["inputs"], valid_name="start")

    violations = []
    for cycle in range(TIMEOUT_CYCLES):
        # 1. Registered outputs: sample at posedge, re-check at negedge
        await RisingEdge(dut.clk)
        for port_name in REGISTERED_OUTPUTS:
            try:
                sig = getattr(dut, port_name)
                posedge_val = int(sig.value)
                await FallingEdge(dut.clk)
                negedge_val = int(sig.value)
                if posedge_val != negedge_val:
                    violations.append(
                        f"cycle={cycle} {port_name}: changed between "
                        f"posedge (0x{posedge_val:x}) and negedge (0x{negedge_val:x}) "
                        f"-- registered output must be stable"
                    )
            except AttributeError:
                pass

        # Stop early if valid was asserted (operation complete)
        try:
            if int(dut.valid.value) == 1:
                break
        except AttributeError:
            pass

        if any(v.startswith("cycle") for v in violations):
            break

    if violations:
        dut._log.error(f"[TIMING CONTRACT] {len(violations)} violation(s):")
        for v in violations[:5]:
            dut._log.error(f"  {v}")
        FAIL_COUNT += len(violations)
        raise AssertionError(
            f"Timing contract violated: {violations[0]}"
        )
    else:
        dut._log.info("[TIMING CONTRACT] PASS -- all invariants hold")


@cocotb.test()
async def test_summary(dut):
    """Final test: reports overall pass/fail.

    MUST be the last test defined in this file -- cocotb runs tests in
    definition order. This test reads the accumulated FAIL_COUNT.
    """
    global FAIL_COUNT
    await ensure_clock(dut)
    await RisingEdge(dut.clk)
    if FAIL_COUNT == 0:
        dut._log.info("ALL TESTS PASSED")
    else:
        dut._log.error(f"FAILED: {FAIL_COUNT} assertion(s) failed")
