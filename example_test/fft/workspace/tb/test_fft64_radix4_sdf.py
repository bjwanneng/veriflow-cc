def _lookup_signal_width(sig_name, default=16):
    """Best-effort width lookup from spec-populated port dicts."""
    seen = {sig_name}
    candidates = [sig_name]
    cursor = 0
    while cursor < len(candidates):
        n = candidates[cursor]
        cursor += 1
        for stripped in (
            n.rsplit(".", 1)[-1] if "." in n else "",
            n[:-4] if n.endswith("_reg") else "",
            n.split("[", 1)[0] if "[" in n else "",
        ):
            if stripped and stripped not in seen:
                seen.add(stripped)
                candidates.append(stripped)

    for name in candidates:
        if name in OUTPUT_PORTS:
            return OUTPUT_PORTS[name]
    for name in candidates:
        if name in INPUT_PORTS:
            return INPUT_PORTS[name]
    return default


async def reset_dut(dut):
    """Apply async active-low reset and wait for outputs to settle."""
    global FAIL_COUNT
    global _STIMULUS_DELIVERED
    _STIMULUS_DELIVERED = False

    # Async active-low reset
    dut.rst_n.value = 0
    # Zero all input ports
    for name in INPUT_PORTS:
        try:
            sig = getattr(dut, name)
            if hasattr(sig, 'value') and hasattr(sig, 'setimmediatevalue'):
                sig.value = 0
        except Exception:
            pass
    await ClockCycles(dut.clk, 3)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)  # rst_n deassert sampled, outputs stable


async def drive_fft_inputs(dut, samples_re, samples_im):
    """Drive 64 input samples with in_valid=1, continuous streaming.

    Timing:
    1. Drive in_valid=1 and first sample
    2. Each RisingEdge: advance to next sample
    3. After all 64 samples, deassert in_valid

    After this function returns, the DUT pipeline is full and will produce
    64 output samples starting PIPELINE_LATENCY cycles after the first
    in_valid was asserted.

    Args:
        dut: cocotb DUT handle
        samples_re: list of 64 Q1.15 unsigned values for real part
        samples_im: list of 64 Q1.15 unsigned values for imaginary part
    """
    global _STIMULUS_DELIVERED

    assert len(samples_re) == N and len(samples_im) == N, (
        f"Expected {N} samples, got re={len(samples_re)} im={len(samples_im)}"
    )

    dut.in_valid.value = 0
    dut.in_data_re.value = 0
    dut.in_data_im.value = 0
    await RisingEdge(dut.clk)

    # Drive all 64 samples with in_valid=1
    for i in range(N):
        dut.in_valid.value = 1
        dut.in_data_re.value = int(samples_re[i])
        dut.in_data_im.value = int(samples_im[i])
        await RisingEdge(dut.clk)

    # Deassert in_valid after all samples driven
    dut.in_valid.value = 0
    dut.in_data_re.value = 0
    dut.in_data_im.value = 0

    _STIMULUS_DELIVERED = True


async def capture_fft_outputs(dut, max_wait_cycles=300):
    """Capture all 64 FFT output samples when out_valid=1.

    Waits for PIPELINE_LATENCY cycles first (pipeline filling), then
    captures up to 64 samples with out_valid=1.

    Returns:
        list of dicts with keys: re, im, index (all integers, unsigned Q1.15)
    """
    outputs = []

    # Wait for pipeline latency + margin
    for _ in range(PIPELINE_LATENCY):
        await RisingEdge(dut.clk)

    # Wait for additional cycles and capture outputs
    for _ in range(max_wait_cycles):
        await RisingEdge(dut.clk)
        if int(dut.out_valid.value) == 1:
            re_val = int(dut.out_data_re.value)
            im_val = int(dut.out_data_im.value)
            idx_val = int(dut.out_index.value)
            outputs.append({"re": re_val, "im": im_val, "index": idx_val})
            if len(outputs) >= N:
                break

    return outputs


def compare_fft_results(dut, actual_outputs, expected_re, expected_im, test_name):
    """Compare captured FFT outputs against golden model expected values.

    Reports per-sample errors, max error, SQNR, and PASS/FAIL.

    Args:
        dut: cocotb DUT handle (for logging)
        actual_outputs: list of dicts from capture_fft_outputs
        expected_re: list of 64 expected Q1.15 unsigned real values
        expected_im: list of 64 expected Q1.15 unsigned imaginary values
        test_name: string name for log messages

    Returns:
        True if all samples within tolerance, False otherwise
    """
    global FAIL_COUNT

    if len(actual_outputs) < N:
        dut._log.error(
            f"[{test_name}] Only captured {len(actual_outputs)}/{N} output samples"
        )
        FAIL_COUNT += 1
        return False

    divergences = []
    total_sq_err = 0.0
    total_sq_sig = 0.0
    max_err = 0.0
    max_err_idx = -1

    for i in range(N):
        out = actual_outputs[i]
        actual_re_float = q115_to_float(out["re"])
        actual_im_float = q115_to_float(out["im"])
        expected_re_float = q115_to_float(expected_re[i])
        expected_im_float = q115_to_float(expected_im[i])

        err_re = abs(actual_re_float - expected_re_float)
        err_im = abs(actual_im_float - expected_im_float)
        err = max(err_re, err_im)

        total_sq_err += err_re ** 2 + err_im ** 2
        total_sq_sig += expected_re_float ** 2 + expected_im_float ** 2

        if err > max_err:
            max_err = err
            max_err_idx = i

        # Record sample-level divergence if error exceeds threshold
        if err > MAX_ERROR_THRESHOLD:
            if _record_divergence(
                divergences,
                f"out_sample[{i}]",
                (expected_re[i] << 16) | expected_im[i],  # combined for hex display
                (out["re"] << 16) | out["im"],
                i,
                32,  # combined width
            ):
                break

    # Compute SQNR
    if total_sq_err > 0:
        sqnr_db = 10.0 * math.log10(total_sq_sig / total_sq_err)
    else:
        sqnr_db = float('inf')

    # Log per-sample details for the worst sample
    if max_err_idx >= 0:
        out = actual_outputs[max_err_idx]
        dut._log.info(
            f"[{test_name}] Worst sample[{max_err_idx}]: "
            f"expected_re={q115_to_float(expected_re[max_err_idx]):.6f} "
            f"actual_re={q115_to_float(out['re']):.6f} | "
            f"expected_im={q115_to_float(expected_im[max_err_idx]):.6f} "
            f"actual_im={q115_to_float(out['im']):.6f} | "
            f"max_err={max_err:.6f}"
        )

    # Determine pass/fail
    passed = max_err <= MAX_ERROR_THRESHOLD and sqnr_db >= SQNR_MIN_DB
    status = "PASS" if passed else "FAIL"

    dut._log.info(
        f"[{status}] {test_name}: max_err={max_err:.6f}, SQNR={sqnr_db:.1f}dB, "
        f"samples={len(actual_outputs)}/{N}"
    )

    if not passed:
        FAIL_COUNT += 1
        # Log detailed divergences
        for div in divergences[:MAX_COLLECTED_DIVERGENCES]:
            idx = div["cycle"]
            out = actual_outputs[idx]
            dut._log.error(
                f"[{test_name}] sample[{idx}] index={out['index']}: "
                f"expected_re=0x{expected_re[idx]:04x} actual_re=0x{out['re']:04x} | "
                f"expected_im=0x{expected_im[idx]:04x} actual_im=0x{out['im']:04x}"
            )

    return passed


