@cocotb.test()
async def test_reset(dut):
    """Test 1: Reset behavior — outputs quiescent after reset."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    reset_ok = True
    for port_name in OUTPUT_PORTS:
        try:
            sig = getattr(dut, port_name)
            val = int(sig.value)
            if val != 0:
                FAIL_COUNT += 1
                dut._log.error(f"[RESET] {port_name}={val} after reset, expected 0")
                reset_ok = False
        except AttributeError:
            dut._log.warning(f"[RESET] Port {port_name} not found in DUT")
    if reset_ok:
        dut._log.info("[PASS] reset behavior correct")


@cocotb.test()
async def test_internal_signals(dut):
    """Test 2: Per-cycle internal signal comparison against golden model.

    This is the KEY verification test. Drives all 3 test vectors and compares
    internal registers (state_reg, round_reg, valid_reg, data_out_reg) against
    the golden model trace on EVERY clock cycle via VPI.

    Timing:
      - drive_aes_start() samples start, DUT now at golden[1] (ROUND_0)
      - Compare golden[1:] against DUT, advancing 1 posedge per golden cycle
      - data_in/key_in held throughout; cleared after comparison
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)

    all_passed = True
    total_cycles_checked = 0
    total_signals_compared = 0

    for vec_idx, tv in enumerate(TEST_VECTORS):
        await reset_dut(dut)

        tv_name = tv["name"]
        dut._log.info(f"[INTERNAL] Vector {vec_idx}: {tv_name}")

        # Drive AES start
        await drive_aes_start(dut, tv["inputs"]["data_in"], tv["inputs"]["key_in"])

        # Get golden trace for this vector
        golden = golden_run(vec_idx)
        if not golden:
            dut._log.info(f"[SKIP] test_internal_signals: golden_run({vec_idx}) returned no data")
            dut.data_in.value = 0
            dut.key_in.value = 0
            continue

        dut._log.info(f"[INTERNAL] Vector {vec_idx}: comparing {len(golden)} golden cycles (skip reset)")

        # Pre-validate signal names
        _validate_trace_signal_names(golden, dut, "test_internal_signals")

        # Alignment: skip golden cycle 0 (reset state)
        compare_cycles = _align_trace(golden)

        divergences: list[dict] = []
        signals_not_found = []

        for cycle_idx, expected in enumerate(compare_cycles):
            stop_outer = False
            for sig_name, expected_val in expected.items():
                # Skip string identifiers
                if sig_name == "fsm_state_name":
                    continue

                # Skip input ports (not internal signals)
                if sig_name in INPUT_PORTS:
                    continue

                try:
                    # Navigate VPI hierarchy for module-qualified names
                    if '.' in sig_name:
                        parts = sig_name.split('.')
                        obj = dut
                        for part in parts:
                            while '[' in part:
                                arr_name, rest = part.split('[', 1)
                                if arr_name:
                                    obj = getattr(obj, arr_name)
                                idx_str, part = rest.split(']', 1)
                                obj = obj[int(idx_str)]
                            if part:
                                obj = getattr(obj, part)
                        actual_val = int(obj.value)
                    else:
                        sig = getattr(dut, sig_name)
                        actual_val = int(sig.value)

                    total_signals_compared += 1
                    if actual_val != expected_val:
                        # Try to get signal width for proper hex formatting
                        try:
                            sig_width = len(obj if '.' in sig_name else sig)
                        except Exception:
                            sig_width = _lookup_signal_width(sig_name)
                        if _record_divergence(
                            divergences, sig_name, expected_val, actual_val,
                            cycle_idx, sig_width,
                        ):
                            stop_outer = True
                            break
                except (AttributeError, IndexError):
                    if sig_name not in signals_not_found:
                        signals_not_found.append(sig_name)

            total_cycles_checked += 1
            if stop_outer:
                break

            # Advance DUT one cycle for the NEXT golden entry
            await RisingEdge(dut.clk)

        # Clear held data inputs
        dut.data_in.value = 0
        dut.key_in.value = 0

        # Warn about signals that could not be found
        if signals_not_found:
            dut._log.warning(
                f"[INTERNAL] vec={vec_idx}: {len(signals_not_found)} signal(s) not found, "
                f"skipped: {signals_not_found[:5]}"
            )

        if divergences:
            FAIL_COUNT += len(divergences)
            all_passed = False
            dut._log.error(
                f"[INTERNAL] Vector {vec_idx} ({tv_name}): "
                f"{len(divergences)} divergence(s) found"
            )
            # Report first divergence but continue to next vector
            for d in divergences[:1]:
                dut._log.error(_format_divergence_block(d, "INTERNAL"))
        else:
            dut._log.info(
                f"[INTERNAL] Vector {vec_idx} ({tv_name}): PASS — "
                f"{total_signals_compared} signal values matched across {total_cycles_checked} cycles"
            )

    # Stimulus sanity check
    if not _STIMULUS_DELIVERED:
        raise AssertionError(
            "test_internal_signals: drive_aes_start() was never called (no stimulus delivered)"
        )

    if total_signals_compared == 0:
        dut._log.error(
            "[INTERNAL] ZERO signals were compared — golden trace signal names "
            "do not match any DUT signals. Check naming convention (_reg suffix)."
        )
        raise AssertionError(
            "test_internal_signals: no signals compared, trace names may not match RTL"
        )

    if all_passed:
        dut._log.info(
            f"[INTERNAL] ALL 3 VECTORS PASSED — "
            f"{total_signals_compared} signal values matched across all cycles"
        )


@cocotb.test()
async def test_layered(dut):
    """Test 3: Layered verification — output port comparison against golden model.

    Compares top-level output ports (valid, data_out) against golden model
    registered output values (valid_reg, data_out_reg) per cycle.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_layered: golden_model.py not available")
        return

    await ensure_clock(dut)

    # Map golden trace signal names to DUT top-level port names
    GOLDEN_TO_PORT = {
        "valid_reg": "valid",
        "data_out_reg": "data_out",
    }

    all_passed = True
    total_cycles_checked = 0
    total_signals_compared = 0

    for vec_idx, tv in enumerate(TEST_VECTORS):
        await reset_dut(dut)

        tv_name = tv["name"]
        dut._log.info(f"[LAYERED] Vector {vec_idx}: {tv_name}")

        await drive_aes_start(dut, tv["inputs"]["data_in"], tv["inputs"]["key_in"])

        golden = golden_run(vec_idx)
        compare_cycles = _align_trace(golden)

        divergences: list[dict] = []
        signals_not_found = []

        for cycle_idx, expected in enumerate(compare_cycles):
            stop_outer = False
            for sig_name, expected_val in expected.items():
                dut_name = GOLDEN_TO_PORT.get(sig_name)
                if dut_name is None:
                    continue
                try:
                    sig = getattr(dut, dut_name)
                    actual_val = int(sig.value)
                    total_signals_compared += 1
                    if actual_val != expected_val:
                        if _record_divergence(
                            divergences, sig_name, expected_val, actual_val,
                            cycle_idx, len(sig),
                        ):
                            stop_outer = True
                            break
                except AttributeError:
                    if dut_name not in signals_not_found:
                        signals_not_found.append(dut_name)

            total_cycles_checked += 1
            if stop_outer:
                break

            await RisingEdge(dut.clk)

        # Clear held data inputs
        dut.data_in.value = 0
        dut.key_in.value = 0

        if signals_not_found:
            dut._log.warning(
                f"[LAYERED] vec={vec_idx}: {len(signals_not_found)} port(s) not found, "
                f"skipped: {signals_not_found[:5]}"
            )

        if divergences:
            FAIL_COUNT += len(divergences)
            all_passed = False
            dut._log.error(
                f"[LAYERED] Vector {vec_idx} ({tv_name}): "
                f"{len(divergences)} divergence(s) found"
            )
            _finalize_divergences(dut, divergences, "LAYERED")
        else:
            dut._log.info(
                f"[LAYERED] Vector {vec_idx} ({tv_name}): PASS"
            )

    if total_signals_compared == 0:
        raise AssertionError(
            "test_layered: ZERO signals were compared — GOLDEN_TO_PORT keys "
            "do not match any golden trace signal names"
        )

    if all_passed:
        dut._log.info(
            f"[LAYERED] ALL 3 VECTORS PASSED — "
            f"{total_signals_compared} signal values matched across {total_cycles_checked} cycles"
        )


@cocotb.test()
async def test_summary(dut):
    """Final test: reports overall pass/fail.

    MUST be the last test defined in this file — cocotb runs tests in
    definition order.
    """
    global FAIL_COUNT
    await ensure_clock(dut)
    await RisingEdge(dut.clk)
    if FAIL_COUNT == 0:
        dut._log.info("ALL TESTS PASSED")
    else:
        dut._log.error(f"FAILED: {FAIL_COUNT} assertion(s) failed")
