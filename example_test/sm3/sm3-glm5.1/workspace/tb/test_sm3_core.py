# ─── Divergence collection ─────────────────────────────────────────────────

MAX_COLLECTED_DIVERGENCES = int(os.environ.get("COCOTB_MAX_DIVERGENCES", "8"))
FAIL_FAST = os.environ.get("COCOTB_FAIL_FAST", "0") == "1"


def _record_divergence(divergences, sig_name, expected_val, actual_val,
                       cycle_idx, width, kind=None):
    """Append a divergence dict. Returns True if caller should stop collecting."""
    div = {
        "cycle":    cycle_idx,
        "signal":   sig_name,
        "expected": expected_val,
        "actual":   actual_val,
        "width":    width,
    }
    if kind:
        div["kind"] = kind
    divergences.append(div)
    return FAIL_FAST or len(divergences) >= MAX_COLLECTED_DIVERGENCES


def _format_divergence_block(div, tag):
    """Format one divergence as a [tag] FIRST DIVERGENCE-shaped block."""
    width = div.get("width") or 32
    hex_digits = max((width + 3) // 4, 1)
    kind = div.get("kind")
    kind_tag = f" kind={kind}" if kind else ""
    hint = ""
    if kind == "stimulus_mismatch":
        hint = "\n  (testbench driver wrong — DUT input doesn't match golden stimulus)"
    return (
        f"[{tag}] FIRST DIVERGENCE at cycle={div['cycle']} "
        f"signal={div['signal']} (width={width}b){kind_tag}:\n"
        f"  expected = 0x{div['expected']:0{hex_digits}x}\n"
        f"  actual   = 0x{div['actual']:0{hex_digits}x}\n"
        f"  xor diff = 0x{(div['expected'] ^ div['actual']):0{hex_digits}x}"
        f"{hint}"
    )


def _finalize_divergences(dut, divergences, tag):
    """Emit each divergence and raise AssertionError with a summary."""
    blocks = [_format_divergence_block(d, tag) for d in divergences]
    for b in blocks:
        dut._log.error(b)
    head = blocks[0]
    if len(blocks) > 1:
        more = "\n\n".join(blocks[1:])
        msg = (
            f"{head}\n\n"
            f"(+{len(blocks) - 1} additional divergence(s) collected; "
            f"set COCOTB_FAIL_FAST=1 to stop at the first):\n\n{more}"
        )
    else:
        msg = head
    raise AssertionError(msg)


# ─── Clock management ──────────────────────────────────────────────────────


async def ensure_clock(dut):
    """Start clock. Must be called at the start of every test."""
    clock = Clock(dut.clk, CLK_PERIOD_NS, unit="ns")
    cocotb.start_soon(clock.start())
    await RisingEdge(dut.clk)


# ─── Reset ─────────────────────────────────────────────────────────────────


async def reset_dut(dut):
    """Apply active-low reset (rst_n). Returns with DUT at golden cycle 0."""
    global _STIMULUS_DELIVERED
    _STIMULUS_DELIVERED = False
    dut.rst_n.value     = 0
    dut.msg_valid.value = 0
    dut.msg_block.value = 0
    dut.is_last.value   = 0
    await ClockCycles(dut.clk, 5)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)      # rst_n deassert sampled, DUT at golden[0]
    # After reset, ready should be combinational 1 (FSM in IDLE).
    # Give one extra negedge half-cycle for NBA to settle before the first test
    # action that samples DUT state. (This also ensures cocotb sees the NBA result
    # from the rst_n deassert posedge.)


# ─── Input driving ─────────────────────────────────────────────────────────


async def drive_sm3_inputs(dut, msg_block_val, is_last_val):
    """Drive one 512-bit block into sm3_core via valid/ready handshake.

    Timing:
      1. Wait for ready=1 (FSM in IDLE, combinational).
      2. Drive msg_block, is_last, msg_valid=1 simultaneously.
      3. await RisingEdge — DUT samples at this posedge. FSM → CALC. DUT at golden[1].
      4. Deassert msg_valid. Data inputs remain stable (held by caller logic).
      5. Return. DUT is at golden cycle 1.

    The caller is responsible for holding msg_block/is_last for
    (DRIVE_PHASE_CYCLES + 1) additional cycles after this function returns,
    then clearing them. This ensures w_gen sees stable data when load_en=1.
    """
    global _STIMULUS_DELIVERED

    # Wait for ready
    timeout = TIMEOUT_CYCLES
    while int(dut.ready.value) != 1 and timeout > 0:
        await RisingEdge(dut.clk)
        timeout -= 1
    assert timeout > 0, "Timeout waiting for ready before drive_sm3_inputs"

    # Drive all inputs at once
    dut.msg_block.value = msg_block_val
    dut.is_last.value   = is_last_val
    dut.msg_valid.value = 1

    # DUT samples at this posedge: FSM transitions IDLE→CALC, load_en asserted.
    # After this RisingEdge, DUT state = golden cycle 1.
    await RisingEdge(dut.clk)
    _STIMULUS_DELIVERED = True

    # Deassert valid (single-cycle pulse per SM3 protocol)
    dut.msg_valid.value = 0
    # Data inputs (msg_block, is_last) remain stable — caller will hold them
    # for DRIVE_PHASE_CYCLES+1 cycles then clear.


# ─── VPI hierarchy access ──────────────────────────────────────────────────


def _read_vpi_signal(dut, hier_path):
    """Read a signal value via VPI hierarchy path.

    Args:
        dut: cocotb DUT handle.
        hier_path: dotted hierarchy path, e.g. "u_sm3_compress.A_reg"
                   or top-level name "ready". Supports array indexing
                   like "u_sm3_w_gen.w_reg[0]".

    Returns:
        int value of the signal, or None if the path cannot be resolved.
    """
    try:
        if '.' in hier_path:
            parts = hier_path.split('.')
            obj = dut
            for part in parts:
                # Handle array indexing: w_reg[0]
                while '[' in part:
                    arr_name, rest = part.split('[', 1)
                    if arr_name:
                        obj = getattr(obj, arr_name)
                    idx_str, part = rest.split(']', 1)
                    idx = int(idx_str)
                    obj = obj[idx]
                if part:
                    obj = getattr(obj, part)
            return int(obj.value)
        elif '[' in hier_path:
            base, rest = hier_path.split('[', 1)
            obj = getattr(dut, base)
            idx_str, part = rest.split(']', 1)
            idx = int(idx_str)
            if part:
                # Nested indexing: base[0].sub_name
                obj = obj[idx]
                while part.startswith('.'):
                    part = part[1:]
                if part:
                    obj = getattr(obj, part)
            else:
                obj = obj[idx]
            return int(obj.value)
        else:
            return int(getattr(dut, hier_path).value)
    except (AttributeError, IndexError, ValueError):
        return None


# ─── Test: Reset Behavior ──────────────────────────────────────────────────


@cocotb.test()
async def test_reset(dut):
    """Test reset behavior: after reset, ready=1, hash_valid=0, hash_out=IV."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset, FSM is IDLE → ready=1 (combinational)
    ready_val = int(dut.ready.value)
    if ready_val != 1:
        FAIL_COUNT += 1
        dut._log.error(f"[RESET] ready={ready_val} after reset, expected 1")
    else:
        dut._log.info("[RESET] ready=1 OK")

    # hash_valid should be 0 after reset
    hv = int(dut.hash_valid.value)
    if hv != 0:
        FAIL_COUNT += 1
        dut._log.error(f"[RESET] hash_valid={hv} after reset, expected 0")
    else:
        dut._log.info("[RESET] hash_valid=0 OK")

    # hash_out should reflect IV (V0-V7 initialized to SM3 IV)
    if GOLDEN_AVAILABLE:
        expected_iv = 0
        for i, v in enumerate(GM_IV):
            expected_iv = (expected_iv << 32) | v
        ho = int(dut.hash_out.value)
        if ho != expected_iv:
            FAIL_COUNT += 1
            dut._log.error(
                f"[RESET] hash_out=0x{ho:064x} after reset, "
                f"expected IV=0x{expected_iv:064x}"
            )
        else:
            dut._log.info("[RESET] hash_out=IV OK")

    if FAIL_COUNT == 0:
        dut._log.info("[PASS] test_reset")


# ─── Test: Protocol Compliance ─────────────────────────────────────────────


@cocotb.test()
async def test_protocol(dut):
    """Test valid/ready handshake protocol compliance."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await reset_dut(dut)

    # After reset, ready=1, hash_valid=0
    hv = int(dut.hash_valid.value)
    if hv != 0:
        FAIL_COUNT += 1
        dut._log.error(f"[PROTOCOL] hash_valid={hv} after reset, expected 0")

    r = int(dut.ready.value)
    if r != 1:
        FAIL_COUNT += 1
        dut._log.error(f"[PROTOCOL] ready={r} after reset, expected 1")

    # After driving a valid input, ready should go low (CALC state)
    if GOLDEN_AVAILABLE and TEST_VECTORS:
        await drive_sm3_inputs(
            dut,
            TEST_VECTORS[0]["inputs"]["msg_block"],
            TEST_VECTORS[0]["inputs"]["is_last"],
        )
        # DUT should now be in CALC, ready=0
        r = int(dut.ready.value)
        if r != 0:
            FAIL_COUNT += 1
            dut._log.error(f"[PROTOCOL] ready={r} during CALC, expected 0")

    if FAIL_COUNT == 0:
        dut._log.info("[PASS] test_protocol")


# ─── Test: Layered (output port comparison) ────────────────────────────────


@cocotb.test()
async def test_layered(dut):
    """Golden model cycle-by-cycle output port comparison.

    Drives both test vectors ("abc" and "empty string") and compares
    top-level output ports (ready, hash_valid, hash_out) against the
    golden model trace at each cycle.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_layered: golden_model.py not available")
        return

    await ensure_clock(dut)

    for tv_idx, tv in enumerate(TEST_VECTORS):
        await reset_dut(dut)
        dut._log.info(f"[LAYERED] Test vector {tv_idx}: {tv['name']}")

        # Get golden trace
        golden_trace = golden_compute(tv["inputs"], trace=True)
        if not golden_trace:
            dut._log.info(f"[SKIP] test_layered: no trace for {tv['name']}")
            continue

        # Drive inputs (DUT → golden[1])
        await drive_sm3_inputs(dut, tv["inputs"]["msg_block"], tv["inputs"]["is_last"])

        # Compare golden[1:] against DUT output ports
        compare_cycles = golden_trace[RESET_CYCLE_SKIP:]  # skip cycle 0
        divergences = []
        cycles_checked = 0
        signals_compared = 0
        hold_remaining = DRIVE_PHASE_CYCLES + 1  # Hold data for 2 cycles

        for cycle_idx, expected in enumerate(compare_cycles):
            stop_outer = False
            for trace_name, dut_name in GOLDEN_TO_PORT.items():
                expected_val = expected.get(trace_name)
                if expected_val is None:
                    continue
                try:
                    actual_val = int(getattr(dut, dut_name).value)
                    signals_compared += 1
                    if actual_val != expected_val:
                        try:
                            width = len(getattr(dut, dut_name))
                        except Exception:
                            width = 256 if "hash_out" in dut_name else 1
                        if _record_divergence(
                            divergences, trace_name, expected_val,
                            actual_val, cycle_idx, width,
                        ):
                            stop_outer = True
                            break
                except AttributeError:
                    pass

            cycles_checked += 1
            if stop_outer:
                break

            # Advance DUT
            await RisingEdge(dut.clk)

            # Hold data for DRIVE_PHASE_CYCLES+1 cycles after sample,
            # then clear. w_gen needs stable msg_block when load_en=1.
            hold_remaining -= 1
            if hold_remaining == 0:
                dut.msg_block.value = 0
                dut.is_last.value   = 0

        if not _STIMULUS_DELIVERED:
            raise AssertionError(
                "test_layered: drive_sm3_inputs() was never called"
            )

        if signals_compared == 0:
            raise AssertionError(
                "test_layered: ZERO signals compared — "
                "GOLDEN_TO_PORT keys do not match DUT ports"
            )

        if divergences:
            FAIL_COUNT += len(divergences)
            _finalize_divergences(dut, divergences, "LAYERED")
        else:
            dut._log.info(
                f"[LAYERED] {tv['name']}: PASS — {cycles_checked} cycles, "
                f"{signals_compared} signal values matched"
            )


# ─── Test: Internal Signals (per-cycle, KEY verification test) ─────────────


@cocotb.test()
async def test_internal_signals(dut):
    """Per-cycle internal signal comparison against golden model.

    This is the KEY verification test. It accesses RTL internal registers
    via VPI hierarchy and compares them against the golden model trace
    at EVERY clock cycle.

    Covers: state_reg, round_cnt, A_reg..H_reg, V0_reg..V7_reg,
            ready, hash_valid, hash_out, load_en, calc_en, update_v_en,
            w_j, w_prime_j.

    Tests BOTH "abc" and "empty string" test vectors.
    """
    global FAIL_COUNT

    if not GOLDEN_AVAILABLE:
        dut._log.info("[SKIP] test_internal_signals: golden_model.py not available")
        return

    await ensure_clock(dut)

    for tv_idx, tv in enumerate(TEST_VECTORS):
        await reset_dut(dut)
        dut._log.info(f"[INTERNAL] Test vector {tv_idx}: {tv['name']}")

        # Get golden trace
        golden_trace = golden_compute(tv["inputs"], trace=True)
        if not golden_trace:
            dut._log.info(f"[SKIP] test_internal_signals: no trace for {tv['name']}")
            continue

        dut._log.info(
            f"[INTERNAL] Comparing {len(golden_trace)} cycles "
            f"({len(GOLDEN_SIGNAL_TO_PATH)} signals/cycle) against golden model"
        )

        # Drive inputs (DUT → golden[1])
        await drive_sm3_inputs(dut, tv["inputs"]["msg_block"], tv["inputs"]["is_last"])

        # Compare golden[1:] against DUT internal signals
        compare_cycles = golden_trace[RESET_CYCLE_SKIP:]  # skip cycle 0
        divergences = []
        cycles_checked = 0
        signals_compared = 0
        signals_not_found = set()
        hold_remaining = DRIVE_PHASE_CYCLES + 1  # Hold data for 2 cycles

        for cycle_idx, expected in enumerate(compare_cycles):
            stop_outer = False

            for trace_name, expected_val in expected.items():
                # Skip input ports; they're our stimulus, not DUT outputs
                if trace_name in INPUT_PORTS:
                    continue

                # Look up VPI path
                hier_path = GOLDEN_SIGNAL_TO_PATH.get(trace_name)
                if hier_path is None:
                    # Signal not in our mapping (e.g., internal implementation detail)
                    continue

                actual_val = _read_vpi_signal(dut, hier_path)
                if actual_val is None:
                    signals_not_found.add(hier_path)
                    continue

                signals_compared += 1
                if actual_val != expected_val:
                    # Determine width for proper hex formatting
                    try:
                        if '.' in hier_path:
                            parts = hier_path.split('.')
                            obj = dut
                            for part in parts:
                                while '[' in part:
                                    arr_name, rest = part.split('[', 1)
                                    if arr_name:
                                        obj = getattr(obj, arr_name)
                                    idx_str, part = rest.split(']', 1)
                                    obj = obj[int(idx_str)]
                                if part:
                                    obj = getattr(obj, part)
                            width = len(obj)
                        else:
                            width = len(getattr(dut, hier_path))
                    except Exception:
                        # Fallback widths for known signals
                        if "hash_out" in trace_name:
                            width = 256
                        elif "reg" in trace_name.lower() or trace_name in ("w_j", "w_prime_j"):
                            width = 32
                        elif "round_cnt" in trace_name:
                            width = 6
                        else:
                            width = 1

                    if _record_divergence(
                        divergences, trace_name, expected_val,
                        actual_val, cycle_idx, width,
                    ):
                        stop_outer = True
                        break

            cycles_checked += 1
            if stop_outer:
                break

            # Advance DUT one cycle
            await RisingEdge(dut.clk)

            # Hold data for DRIVE_PHASE_CYCLES+1 cycles after sample
            hold_remaining -= 1
            if hold_remaining == 0:
                dut.msg_block.value = 0
                dut.is_last.value   = 0

        if not _STIMULUS_DELIVERED:
            raise AssertionError(
                "test_internal_signals: drive_sm3_inputs() was never called"
            )

        if signals_not_found:
            dut._log.warning(
                f"[INTERNAL] {len(signals_not_found)} VPI path(s) not found in DUT, "
                f"skipped: {sorted(signals_not_found)[:10]}"
                + (f" ... and {len(signals_not_found) - 10} more"
                   if len(signals_not_found) > 10 else "")
            )

        if signals_compared == 0:
            dut._log.error(
                "[INTERNAL] ZERO signals compared — golden trace signal names "
                "do not match any GOLDEN_SIGNAL_TO_PATH entries"
            )
            raise AssertionError(
                "test_internal_signals: no signals compared; "
                "check GOLDEN_SIGNAL_TO_PATH mapping"
            )

        if divergences:
            FAIL_COUNT += len(divergences)
            _finalize_divergences(dut, divergences, "INTERNAL")
        else:
            dut._log.info(
                f"[INTERNAL] {tv['name']}: PASS — {cycles_checked} cycles, "
                f"{signals_compared} signal values matched"
            )


# ─── Test: Summary ─────────────────────────────────────────────────────────


@cocotb.test()
async def test_summary(dut):
    """Final test: reports overall pass/fail across all tests."""
    global FAIL_COUNT
    await ensure_clock(dut)
    await RisingEdge(dut.clk)
    if FAIL_COUNT == 0:
        dut._log.info("=" * 60)
        dut._log.info("ALL TESTS PASSED")
        dut._log.info("=" * 60)
    else:
        dut._log.error(f"FAILED: {FAIL_COUNT} assertion(s) failed across all tests")


